from enum import Enum
from typing import Dict, List, Any, Optional
from pydantic import BaseModel, Field


# ============================================================================
# ENUMERATIONS
# ============================================================================

class CDPQuestionType(str, Enum):
    """Supported CDP question field types."""
    TEXT = "text"
    NUMBER = "number"
    SELECT = "select"
    MULTI_SELECT = "multi_select"
    TABLE = "table"
    BOOLEAN = "boolean"
    DATE = "date"
    FILE_UPLOAD = "file_upload"


class CDPScoreLevel(str, Enum):
    """CDP scoring levels from A (Leadership) to D- (Disclosure)."""
    A = "A"
    A_MINUS = "A-"
    B = "B"
    B_MINUS = "B-"
    C = "C"
    C_MINUS = "C-"
    D = "D"
    D_MINUS = "D-"


class CDPScoringBand(str, Enum):
    """CDP scoring band labels."""
    LEADERSHIP = "Leadership"
    MANAGEMENT = "Management"
    AWARENESS = "Awareness"
    DISCLOSURE = "Disclosure"


# ============================================================================
# PYDANTIC MODELS
# ============================================================================

class CDPQuestionOption(BaseModel):
    """Single option for select/multi-select questions."""
    value: str = Field(..., description="Option value")
    label: str = Field(..., description="Display label")
    score_weight: float = Field(default=0.0, description="Score weight for this option")


class CDPValidationRule(BaseModel):
    """Validation rule for a CDP question."""
    rule_type: str = Field(..., description="Validation rule type (required, range, pattern, cross_ref)")
    params: Dict[str, Any] = Field(default_factory=dict, description="Rule parameters")
    message: str = Field(default="", description="Error message when validation fails")


class CDPQuestion(BaseModel):
    """Single CDP questionnaire question definition."""
    id: str = Field(..., description="Question identifier (e.g., C0.1)")
    text: str = Field(..., description="Full question text")
    type: CDPQuestionType = Field(..., description="Input field type")
    required: bool = Field(default=False, description="Whether the question is mandatory")
    auto_populatable: bool = Field(default=False, description="Can be auto-populated from data")
    data_source: Optional[str] = Field(None, description="Data source path for auto-population")
    help_text: str = Field(default="", description="Guidance text for the respondent")
    max_length: Optional[int] = Field(None, description="Max character length for text fields")
    options: List[CDPQuestionOption] = Field(default_factory=list, description="Options for select fields")
    validation_rules: List[CDPValidationRule] = Field(default_factory=list, description="Validation rules")
    score_category: Optional[str] = Field(None, description="Scoring category (disclosure, awareness, management, leadership)")
    conditional_on: Optional[str] = Field(None, description="Question ID that must be answered first")
    table_columns: Optional[List[str]] = Field(None, description="Column headers for table-type questions")


class CDPSection(BaseModel):
    """CDP questionnaire section containing related questions."""
    id: str = Field(..., description="Section identifier (e.g., C0)")
    title: str = Field(..., description="Section title")
    description: str = Field(default="", description="Section description and guidance")
    questions: List[CDPQuestion] = Field(..., description="Questions within this section")
    weight: float = Field(default=1.0, description="Scoring weight for this section")
    critical: bool = Field(default=False, description="Whether section is critical for scoring")


class CDPScoringCriteria(BaseModel):
    """Criteria for a specific CDP score level."""
    level: CDPScoreLevel = Field(..., description="Score level")
    band: CDPScoringBand = Field(..., description="Scoring band")
    min_completion_pct: float = Field(..., ge=0, le=100, description="Minimum completion percentage")
    required_sections: List[str] = Field(..., description="Sections that must be completed")
    required_features: List[str] = Field(..., description="Features required for this level")
    description: str = Field(default="", description="Description of what this score level means")


class CDPQuestionnaireSchema(BaseModel):
    """Complete CDP questionnaire schema definition."""
    version: str = Field(..., description="Questionnaire version year")
    sections: Dict[str, CDPSection] = Field(..., description="All sections keyed by section ID")
    scoring_criteria: Dict[str, CDPScoringCriteria] = Field(
        default_factory=dict, description="Scoring criteria by level"
    )
    total_questions: int = Field(default=0, description="Total number of questions")


