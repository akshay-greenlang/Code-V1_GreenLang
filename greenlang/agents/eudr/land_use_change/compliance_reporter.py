    # ------------------------------------------------------------------
    # Internal: Validation
    # ------------------------------------------------------------------

    def _validate_report_type(self, report_type: str) -> None:
        """Validate report type.

        Raises:
            ValueError: If report_type is not recognized.
        """
        if report_type not in VALID_REPORT_TYPES:
            raise ValueError(
                f"Invalid report_type '{report_type}'. "
                f"Valid types: {sorted(VALID_REPORT_TYPES)}"
            )

    def _validate_format(self, fmt: str) -> None:
        """Validate output format.

        Raises:
            ValueError: If format is not recognized.
        """
        if fmt not in VALID_FORMATS:
            raise ValueError(
                f"Invalid format '{fmt}'. "
                f"Valid formats: {sorted(VALID_FORMATS)}"
            )

    # ------------------------------------------------------------------
    # Internal: Error Report
    # ------------------------------------------------------------------

    def _create_error_report(
        self,
        config: ReportConfig,
        error_msg: str,
    ) -> ComplianceReport:
        """Create an error report for a failed generation.

        Args:
            config: Report configuration that failed.
            error_msg: Error message.

        Returns:
            ComplianceReport with error metadata.
        """
        report = ComplianceReport(
            report_id=_generate_id(),
            report_type=config.report_type,
            format=config.format,
            plot_ids=list(config.plot_ids),
            total_plots=len(config.plot_ids),
            summary=f"Report generation failed: {error_msg}",
            created_at=utcnow().isoformat(),
            metadata={"error": error_msg},
        )
        report.report_hash = _compute_hash({"error": error_msg})
        return report

