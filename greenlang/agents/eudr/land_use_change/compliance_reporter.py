    def _format_csv(self, report_data: Dict[str, Any]) -> str:
        """Format report data as CSV string.

        Args:
            report_data: Report data dictionary.

        Returns:
            CSV string with header row and data rows.
        """
        output = io.StringIO()
        plots = report_data.get("plots", [])

        if not plots:
            report_type = report_data.get("report_type", "")
            if report_type == ReportType.SINGLE_PLOT.value:
                columns = SINGLE_PLOT_CSV_COLUMNS
            else:
                columns = BATCH_CSV_COLUMNS
            writer = csv.DictWriter(output, fieldnames=columns)
            writer.writeheader()
            return output.getvalue()

        all_keys = set()
        for plot in plots:
            if isinstance(plot, dict):
                all_keys.update(plot.keys())

        report_type = report_data.get("report_type", "")
        if report_type == ReportType.SINGLE_PLOT.value:
            columns = SINGLE_PLOT_CSV_COLUMNS
        else:
            columns = BATCH_CSV_COLUMNS

        final_columns = [c for c in columns if c in all_keys or c == "report_id"]
        extra_keys = sorted(all_keys - set(final_columns))
        final_columns.extend(extra_keys)

        writer = csv.DictWriter(
            output,
            fieldnames=final_columns,
            extrasaction="ignore",
        )
        writer.writeheader()

        for plot in plots:
            if isinstance(plot, dict):
                row = dict(plot)
                row.setdefault("report_id", report_data.get("report_id", ""))
                row.setdefault("provenance_hash", _compute_hash(row))
                writer.writerow(row)

        return output.getvalue()

    def _format_pdf(self, report_data: Dict[str, Any]) -> Dict[str, Any]:
        """Format report data as a PDF structure for a renderer.

        Returns a dictionary describing the PDF layout: title, sections,
        tables, charts, and maps.  The actual PDF rendering is performed
        by the downstream PDF renderer service.

        Args:
            report_data: Report data dictionary.

        Returns:
            PDF structure dictionary.
        """
        report_type = report_data.get("report_type", "")
        plots = report_data.get("plots", [])
        verdicts = report_data.get("verdicts", [])

        title = f"EUDR Compliance Report - {report_type}"
        subtitle = (
            f"Generated: {_today_iso()} | "
            f"Regulatory Framework: {REGULATORY_FRAMEWORK}"
        )

        sections: List[Dict[str, Any]] = []

        sections.append({
            "heading": "Executive Summary",
            "content": self._generate_summary(
                report_type=report_type,
                total_plots=len(plots),
                compliant=sum(
                    1 for v in verdicts
                    if v.get("verdict", "") in ("compliant", "deforestation_free")
                ),
                non_compliant=sum(
                    1 for v in verdicts
                    if v.get("verdict", "") == "non_compliant"
                ),
                inconclusive=sum(
                    1 for v in verdicts
                    if v.get("verdict", "") == "inconclusive"
                ),
            ),
        })

        if report_type == ReportType.SINGLE_PLOT.value:
            sections.append({
                "heading": "Plot Details",
                "content": report_data.get("plot_id", ""),
                "table": {
                    "columns": SINGLE_PLOT_CSV_COLUMNS,
                    "rows": plots,
                },
            })
            sections.append({
                "heading": "Before/After Land Use Map",
                "chart_type": "before_after_map",
                "data": report_data.get("before_after_map", {}),
            })

        elif report_type == ReportType.REGIONAL_TRANSITION.value:
            sections.append({
                "heading": "Transition Matrix",
                "chart_type": "transition_matrix",
                "data": report_data.get(
                    "transition_matrix_visualization", {},
                ),
            })

        elif report_type == ReportType.RISK_ASSESSMENT.value:
            sections.append({
                "heading": "Risk Distribution",
                "chart_type": "risk_heatmap",
                "data": report_data.get("tier_distribution", {}),
            })

        sections.append({
            "heading": "Compliance Verdicts",
            "table": {
                "columns": ["plot_id", "verdict", "confidence"],
                "rows": verdicts,
            },
        })

        if plots:
            sections.append({
                "heading": "Detailed Plot Data",
                "table": {
                    "columns": BATCH_CSV_COLUMNS,
                    "rows": plots,
                },
            })

        sections.append({
            "heading": "Regulatory References",
            "content": (
                "This report was generated under the EU Deforestation "
                "Regulation (EU) 2023/1115. The EUDR cutoff date is "
                f"{EUDR_CUTOFF_DATE.isoformat()} per Article 2(1). "
                "Evidence has been compiled per Articles 4, 9, 10, "
                "and 31."
            ),
        })

        return {
            "title": title,
            "subtitle": subtitle,
            "page_size": "A4",
            "orientation": "portrait",
            "sections": sections,
            "footer": (
                f"Report ID: {report_data.get('report_id', _generate_id())} | "
                f"Generated by GreenLang EUDR Land Use Change Agent v{_MODULE_VERSION}"
            ),
        }

    def _format_eudr_xml(self, report_data: Dict[str, Any]) -> str:
        """Format report data as EUDR regulatory XML.

        Produces XML compatible with the EU Information System
        DDS submission schema.

        Args:
            report_data: Report data dictionary.

        Returns:
            XML string.
        """
        root = ET.Element("EUDRComplianceReport")
        root.set("xmlns", "urn:eu:eudr:compliance:v1")
        root.set("version", _MODULE_VERSION)

        header = ET.SubElement(root, "Header")
        ET.SubElement(header, "ReportId").text = _generate_id()
        ET.SubElement(header, "ReportType").text = report_data.get(
            "report_type", "",
        )
        ET.SubElement(header, "RegulatoryFramework").text = REGULATORY_FRAMEWORK
        ET.SubElement(header, "CutoffDate").text = EUDR_CUTOFF_DATE.isoformat()
        ET.SubElement(header, "AssessmentDate").text = _today_iso()
        ET.SubElement(header, "GeneratedAt").text = utcnow().isoformat()

        summary_elem = ET.SubElement(root, "Summary")
        ET.SubElement(summary_elem, "TotalPlots").text = str(
            report_data.get("total_plots", 0),
        )

        verdicts = report_data.get("verdicts", [])
        compliant = sum(
            1 for v in verdicts
            if v.get("verdict", "") in ("compliant", "deforestation_free")
        )
        non_compliant = sum(
            1 for v in verdicts
            if v.get("verdict", "") == "non_compliant"
        )
        inconclusive = sum(
            1 for v in verdicts
            if v.get("verdict", "") == "inconclusive"
        )
        ET.SubElement(summary_elem, "Compliant").text = str(compliant)
        ET.SubElement(summary_elem, "NonCompliant").text = str(non_compliant)
        ET.SubElement(summary_elem, "Inconclusive").text = str(inconclusive)

        plots_elem = ET.SubElement(root, "Plots")
        plots = report_data.get("plots", [])
        for plot_data in plots:
            if not isinstance(plot_data, dict):
                continue
            plot_elem = ET.SubElement(plots_elem, "Plot")
            ET.SubElement(plot_elem, "PlotId").text = str(
                plot_data.get("plot_id", ""),
            )
            ET.SubElement(plot_elem, "Latitude").text = str(
                plot_data.get("latitude", 0.0),
            )
            ET.SubElement(plot_elem, "Longitude").text = str(
                plot_data.get("longitude", 0.0),
            )
            ET.SubElement(plot_elem, "AreaHa").text = str(
                plot_data.get("area_ha", 0.0),
            )
            ET.SubElement(plot_elem, "Verdict").text = str(
                plot_data.get("verdict", ""),
            )
            ET.SubElement(plot_elem, "Confidence").text = str(
                plot_data.get("confidence", 0.0),
            )
            ET.SubElement(plot_elem, "RiskTier").text = str(
                plot_data.get("risk_tier", ""),
            )
            ET.SubElement(plot_elem, "RiskScore").text = str(
                plot_data.get("risk_score", 0.0),
            )
            ET.SubElement(plot_elem, "Commodity").text = str(
                plot_data.get("commodity", ""),
            )
            ET.SubElement(plot_elem, "TransitionDetected").text = str(
                plot_data.get("transition_detected", False),
            ).lower()

        evidence_elem = ET.SubElement(root, "Evidence")
        ET.SubElement(evidence_elem, "ProvenanceHash").text = _compute_hash(
            report_data,
        )
        ET.SubElement(evidence_elem, "ModuleVersion").text = _MODULE_VERSION

        tree_str = ET.tostring(root, encoding="unicode", xml_declaration=False)
        return f'<?xml version="1.0" encoding="UTF-8"?>\n{tree_str}'

