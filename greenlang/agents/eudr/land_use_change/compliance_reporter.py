    # ------------------------------------------------------------------
    # Internal: Non-Compliance Flagging
    # ------------------------------------------------------------------

    def _flag_non_compliant(
        self,
        verdicts: List[Dict[str, Any]],
    ) -> List[Dict[str, Any]]:
        """Flag non-compliant plots from verdict list.

        Args:
            verdicts: List of verdict dictionaries.

        Returns:
            List of non-compliant plot flag dictionaries.
        """
        flags: List[Dict[str, Any]] = []
        for verdict in verdicts:
            v = verdict.get("verdict", "").lower()
            if v in ("non_compliant", "deforested", "degraded"):
                flags.append({
                    "plot_id": verdict.get("plot_id", ""),
                    "verdict": v,
                    "confidence": verdict.get("confidence", 0.0),
                    "flag_type": "NON_COMPLIANT",
                    "action_required": (
                        "Exclude from supply chain or obtain "
                        "remediation evidence per EUDR Article 10."
                    ),
                })
        return flags

    # ------------------------------------------------------------------
    # Internal: Summary Generation
    # ------------------------------------------------------------------

    def _generate_summary(
        self,
        report_type: str,
        total_plots: int,
        compliant: int,
        non_compliant: int,
        inconclusive: int,
    ) -> str:
        """Generate a template-based narrative summary.

        No LLM is used; summary is composed from computed values
        using deterministic templates.

        Args:
            report_type: Type of report.
            total_plots: Total number of plots.
            compliant: Compliant count.
            non_compliant: Non-compliant count.
            inconclusive: Inconclusive count.

        Returns:
            Narrative summary string.
        """
        compliance_rate = (
            compliant / max(total_plots, 1) * 100.0
        )

        lines = [
            f"EUDR Compliance Report ({report_type})",
            f"Assessment Date: {_today_iso()}",
            f"Regulatory Framework: {REGULATORY_FRAMEWORK}",
            f"EUDR Cutoff Date: {EUDR_CUTOFF_DATE.isoformat()}",
            "",
            f"Total Plots Assessed: {total_plots}",
            f"Compliant: {compliant} ({compliance_rate:.1f}%)",
            f"Non-Compliant: {non_compliant}",
            f"Inconclusive: {inconclusive}",
        ]

        if non_compliant > 0:
            lines.append("")
            lines.append(
                f"WARNING: {non_compliant} plot(s) flagged as "
                f"non-compliant with EUDR deforestation-free "
                f"requirements. Immediate supply chain review "
                f"recommended per Article 10."
            )

        if inconclusive > 0:
            lines.append("")
            lines.append(
                f"NOTE: {inconclusive} plot(s) have inconclusive "
                f"results due to insufficient data quality. "
                f"Additional evidence collection recommended."
            )

        return "\n".join(lines)

