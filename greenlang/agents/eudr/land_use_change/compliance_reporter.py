class ComplianceReporter:
    """Generates regulatory-grade evidence packages for DDS submission.

    Supports 5 report types, 4 output formats, and complete evidence
    chains with SHA-256 provenance hashing.  Integrates with
    GL-EUDR-APP DDS Reporting Engine.

    All report content is assembled from deterministic analysis results.
    No LLM is used for numeric computations.

    Example::

        reporter = ComplianceReporter()
        config = ReportConfig(
            report_type="SINGLE_PLOT",
            plot_ids=["plot-001"],
            format="JSON",
        )
        report = reporter.generate_report(
            report_type=config.report_type,
            plot_ids=config.plot_ids,
            format=config.format,
            options={"analysis_results": results_dict},
        )
        assert report.report_hash != ""

    Attributes:
        config: Optional agent configuration object.
    """

    def __init__(self, config: Any = None) -> None:
        """Initialize the ComplianceReporter.

        Args:
            config: Optional LandUseChangeConfig instance.
        """
        self.config = config
        logger.info(
            "ComplianceReporter initialized: module_version=%s",
            _MODULE_VERSION,
        )

    # ------------------------------------------------------------------
    # Public API: Report Generation
    # ------------------------------------------------------------------

    def generate_report(
        self,
        report_type: str,
        plot_ids: List[str],
        format: str = ReportFormat.JSON.value,
        options: Optional[Dict[str, Any]] = None,
    ) -> ComplianceReport:
        """Generate a compliance report.

        Dispatches to the appropriate report generator based on
        report_type and formats the output according to format.

        Args:
            report_type: Report type (SINGLE_PLOT, REGIONAL_TRANSITION,
                COMMODITY_CONVERSION, CUTOFF_COMPLIANCE, RISK_ASSESSMENT).
            plot_ids: List of plot identifiers.
            format: Output format (JSON, CSV, PDF, EUDR_XML).
            options: Optional dictionary with analysis_results and
                configuration overrides.

        Returns:
            ComplianceReport with formatted content and provenance.

        Raises:
            ValueError: If report_type or format is invalid.
        """
        start_time = time.monotonic()
        self._validate_report_type(report_type)
        self._validate_format(format)

        opts = options or {}
        analysis_results = opts.get("analysis_results", {})
        region = opts.get("region", {})
        commodity = opts.get("commodity", "")
        date_from = opts.get("date_from", "")
        date_to = opts.get("date_to", "")

        if report_type == ReportType.SINGLE_PLOT.value:
            report_data = self._generate_single_plot_report(
                plot_ids, analysis_results,
            )
        elif report_type == ReportType.REGIONAL_TRANSITION.value:
            report_data = self._generate_regional_transition_report(
                region, date_from, date_to, analysis_results,
            )
        elif report_type == ReportType.COMMODITY_CONVERSION.value:
            report_data = self._generate_commodity_conversion_report(
                commodity, region, analysis_results,
            )
        elif report_type == ReportType.CUTOFF_COMPLIANCE.value:
            report_data = self._generate_cutoff_compliance_report(
                plot_ids, analysis_results,
            )
        elif report_type == ReportType.RISK_ASSESSMENT.value:
            report_data = self._generate_risk_assessment_report(
                plot_ids, analysis_results,
            )
        else:
            raise ValueError(f"Unsupported report_type: {report_type}")

        evidence_chain = self._compile_evidence_chain(report_data)

        if format == ReportFormat.JSON.value:
            content = self._format_json(report_data)
        elif format == ReportFormat.CSV.value:
            content = self._format_csv(report_data)
        elif format == ReportFormat.PDF.value:
            pdf_structure = self._format_pdf(report_data)
            content = json.dumps(pdf_structure, indent=2, default=str)
        elif format == ReportFormat.EUDR_XML.value:
            content = self._format_eudr_xml(report_data)
        else:
            content = self._format_json(report_data)

        report_hash = self._compute_report_hash(report_data)

        verdicts = report_data.get("verdicts", [])
        non_compliant = self._flag_non_compliant(verdicts)

        compliant_count = sum(
            1 for v in verdicts
            if v.get("verdict", "").lower() in (
                "compliant", "deforestation_free",
            )
        )
        non_compliant_count = len(non_compliant)
        inconclusive_count = sum(
            1 for v in verdicts
            if v.get("verdict", "").lower() == "inconclusive"
        )

        summary = self._generate_summary(
            report_type=report_type,
            total_plots=len(plot_ids),
            compliant=compliant_count,
            non_compliant=non_compliant_count,
            inconclusive=inconclusive_count,
        )

        from datetime import timedelta

        valid_until = (
            datetime.now(timezone.utc).date()
            + timedelta(days=DEFAULT_VALIDITY_DAYS)
        )

        elapsed_ms = (time.monotonic() - start_time) * 1000

        report = ComplianceReport(
            report_id=_generate_id(),
            report_type=report_type,
            format=format,
            plot_ids=list(plot_ids),
            total_plots=len(plot_ids),
            compliant_count=compliant_count,
            non_compliant_count=non_compliant_count,
            inconclusive_count=inconclusive_count,
            summary=summary,
            content=content,
            evidence_chain=evidence_chain,
            report_hash=report_hash,
            regulatory_framework=REGULATORY_FRAMEWORK,
            created_at=utcnow().isoformat(),
            valid_until=valid_until.isoformat(),
            reviewer=opts.get("reviewer", ""),
            processing_time_ms=round(elapsed_ms, 2),
            metadata={
                "module_version": _MODULE_VERSION,
                "commodity": commodity,
                "region": region,
                "date_from": date_from,
                "date_to": date_to,
                "non_compliant_flags": non_compliant,
            },
        )

        logger.info(
            "Report generated: type=%s, format=%s, %d plots, "
            "compliant=%d, non_compliant=%d, inconclusive=%d, "
            "%.2fms",
            report_type, format, len(plot_ids),
            compliant_count, non_compliant_count,
            inconclusive_count, elapsed_ms,
        )
        return report

    # ------------------------------------------------------------------
    # Public API: Batch Report Generation
    # ------------------------------------------------------------------

    def generate_batch_reports(
        self,
        report_configs: List[ReportConfig],
    ) -> List[ComplianceReport]:
        """Generate multiple reports from a list of configurations.

        Args:
            report_configs: List of ReportConfig objects.

        Returns:
            List of ComplianceReport objects.

        Raises:
            ValueError: If report_configs is empty.
        """
        if not report_configs:
            raise ValueError("report_configs list must not be empty")

        start_time = time.monotonic()
        reports: List[ComplianceReport] = []

        for i, config in enumerate(report_configs):
            try:
                report = self.generate_report(
                    report_type=config.report_type,
                    plot_ids=config.plot_ids,
                    format=config.format,
                    options={
                        "commodity": config.commodity,
                        "region": config.region,
                        "date_from": config.date_from,
                        "date_to": config.date_to,
                        "reviewer": config.reviewer,
                    },
                )
                reports.append(report)
            except Exception as exc:
                logger.error(
                    "generate_batch_reports: failed on config[%d]: %s",
                    i, str(exc),
                )
                reports.append(self._create_error_report(
                    config, str(exc),
                ))

        elapsed_ms = (time.monotonic() - start_time) * 1000
        logger.info(
            "Batch reports: %d generated, %.2fms",
            len(reports), elapsed_ms,
        )
        return reports

