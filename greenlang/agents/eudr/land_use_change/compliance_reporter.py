    def _compile_evidence_chain(
        self,
        results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Compile complete evidence chain with all input hashes.

        Args:
            results: Report data dictionary.

        Returns:
            Evidence chain dictionary.
        """
        plots = results.get("plots", [])
        input_hashes: List[str] = []

        for plot in plots:
            if isinstance(plot, dict):
                plot_hash = _compute_hash(plot)
                input_hashes.append(plot_hash)

        chain_hash = _compute_hash({
            "input_hashes": input_hashes,
            "report_type": results.get("report_type", ""),
            "assessment_date": results.get("assessment_date", ""),
        })

        return {
            "input_count": len(input_hashes),
            "input_hashes": input_hashes,
            "chain_hash": chain_hash,
            "module_version": _MODULE_VERSION,
            "timestamp": utcnow().isoformat(),
        }

