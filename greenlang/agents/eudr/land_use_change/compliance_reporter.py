    def _generate_single_plot_report(
        self,
        plot_ids: List[str],
        analysis_results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Generate single-plot report data.

        Args:
            plot_ids: Plot identifiers (uses first plot).
            analysis_results: Analysis results dictionary.

        Returns:
            Report data dictionary.
        """
        plot_id = plot_ids[0] if plot_ids else "unknown"
        plot_data = analysis_results.get(plot_id, {})

        land_use_at_cutoff = plot_data.get("land_use_at_cutoff", "unknown")
        land_use_current = plot_data.get("land_use_current", "unknown")
        transition_detected = plot_data.get("transition_detected", False)
        confidence = plot_data.get("confidence", 0.0)
        risk_score = plot_data.get("risk_score", 0.0)
        risk_tier = plot_data.get("risk_tier", "MODERATE")
        latitude = plot_data.get("latitude", 0.0)
        longitude = plot_data.get("longitude", 0.0)
        area_ha = plot_data.get("area_ha", 0.0)
        data_quality = plot_data.get("data_quality_tier", "SILVER")

        if transition_detected:
            verdict = "non_compliant"
        elif confidence < 0.5:
            verdict = "inconclusive"
        else:
            verdict = "compliant"

        return {
            "report_type": ReportType.SINGLE_PLOT.value,
            "plot_id": plot_id,
            "latitude": latitude,
            "longitude": longitude,
            "area_ha": area_ha,
            "land_use_at_cutoff": land_use_at_cutoff,
            "land_use_current": land_use_current,
            "transition_detected": transition_detected,
            "compliance_verdict": verdict,
            "confidence": confidence,
            "risk_tier": risk_tier,
            "risk_score": risk_score,
            "data_quality_tier": data_quality,
            "cutoff_date": EUDR_CUTOFF_DATE.isoformat(),
            "assessment_date": _today_iso(),
            "before_after_map": self._generate_before_after_map(
                land_use_at_cutoff, land_use_current,
            ),
            "verdicts": [
                {
                    "plot_id": plot_id,
                    "verdict": verdict,
                    "confidence": confidence,
                },
            ],
            "plots": [{
                "plot_id": plot_id,
                "latitude": latitude,
                "longitude": longitude,
                "area_ha": area_ha,
                "verdict": verdict,
                "confidence": confidence,
                "risk_tier": risk_tier,
                "risk_score": risk_score,
                "transition_detected": transition_detected,
                "commodity": plot_data.get("commodity", ""),
                "assessment_date": _today_iso(),
            }],
        }

    def _generate_regional_transition_report(
        self,
        region: Dict[str, float],
        date_from: str,
        date_to: str,
        analysis_results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Generate regional transition report data.

        Args:
            region: Region bounds.
            date_from: Analysis start date.
            date_to: Analysis end date.
            analysis_results: Analysis results dictionary.

        Returns:
            Report data dictionary.
        """
        plots = list(analysis_results.values()) if analysis_results else []
        transition_matrix = analysis_results.get("transition_matrix", {})

        verdicts: List[Dict[str, Any]] = []
        for plot_data in plots:
            if isinstance(plot_data, dict) and "plot_id" in plot_data:
                verdict = "compliant"
                if plot_data.get("transition_detected", False):
                    verdict = "non_compliant"
                elif plot_data.get("confidence", 0.0) < 0.5:
                    verdict = "inconclusive"
                verdicts.append({
                    "plot_id": plot_data.get("plot_id", ""),
                    "verdict": verdict,
                    "confidence": plot_data.get("confidence", 0.0),
                })

        matrix_viz = self._generate_transition_matrix_viz(transition_matrix)

        return {
            "report_type": ReportType.REGIONAL_TRANSITION.value,
            "region": region,
            "date_from": date_from,
            "date_to": date_to,
            "total_plots": len(plots),
            "transition_matrix": transition_matrix,
            "transition_matrix_visualization": matrix_viz,
            "verdicts": verdicts,
            "assessment_date": _today_iso(),
            "plots": [
                p for p in plots
                if isinstance(p, dict) and "plot_id" in p
            ],
        }

    def _generate_commodity_conversion_report(
        self,
        commodity: str,
        region: Dict[str, float],
        analysis_results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Generate commodity-specific conversion report data.

        Args:
            commodity: Target commodity.
            region: Region bounds.
            analysis_results: Analysis results dictionary.

        Returns:
            Report data dictionary.
        """
        plots = list(analysis_results.values()) if analysis_results else []
        conversions = [
            p for p in plots
            if isinstance(p, dict) and p.get("conversion_detected", False)
        ]

        verdicts: List[Dict[str, Any]] = []
        for plot_data in plots:
            if isinstance(plot_data, dict) and "plot_id" in plot_data:
                verdict = "compliant"
                if plot_data.get("conversion_detected", False):
                    verdict = "non_compliant"
                elif plot_data.get("confidence", 0.0) < 0.5:
                    verdict = "inconclusive"
                verdicts.append({
                    "plot_id": plot_data.get("plot_id", ""),
                    "verdict": verdict,
                    "confidence": plot_data.get("confidence", 0.0),
                })

        total_conversion_area = sum(
            c.get("area_ha", 0.0) for c in conversions
        )

        return {
            "report_type": ReportType.COMMODITY_CONVERSION.value,
            "commodity": commodity,
            "region": region,
            "total_plots": len(plots),
            "conversion_count": len(conversions),
            "total_conversion_area_ha": round(total_conversion_area, 2),
            "conversion_rate_pct": round(
                len(conversions) / max(len(plots), 1) * 100.0, 2,
            ),
            "verdicts": verdicts,
            "assessment_date": _today_iso(),
            "plots": [
                p for p in plots
                if isinstance(p, dict) and "plot_id" in p
            ],
        }

    def _generate_cutoff_compliance_report(
        self,
        plot_ids: List[str],
        analysis_results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Generate EUDR cutoff date compliance report data.

        Args:
            plot_ids: Plot identifiers.
            analysis_results: Analysis results dictionary.

        Returns:
            Report data dictionary.
        """
        verdicts: List[Dict[str, Any]] = []
        plots_data: List[Dict[str, Any]] = []

        for plot_id in plot_ids:
            plot_data = analysis_results.get(plot_id, {})
            was_forest = plot_data.get("was_forest_at_cutoff", False)
            is_forest = plot_data.get("is_forest_current", False)
            transition = plot_data.get("transition_detected", False)
            confidence = plot_data.get("confidence", 0.0)

            if was_forest and not is_forest and transition:
                verdict = "non_compliant"
            elif confidence < 0.5:
                verdict = "inconclusive"
            else:
                verdict = "compliant"

            verdicts.append({
                "plot_id": plot_id,
                "verdict": verdict,
                "confidence": confidence,
                "was_forest_at_cutoff": was_forest,
                "is_forest_current": is_forest,
            })
            plots_data.append({
                "plot_id": plot_id,
                "verdict": verdict,
                "confidence": confidence,
                "was_forest_at_cutoff": was_forest,
                "is_forest_current": is_forest,
                "transition_detected": transition,
                "latitude": plot_data.get("latitude", 0.0),
                "longitude": plot_data.get("longitude", 0.0),
                "area_ha": plot_data.get("area_ha", 0.0),
                "commodity": plot_data.get("commodity", ""),
                "risk_tier": plot_data.get("risk_tier", ""),
                "risk_score": plot_data.get("risk_score", 0.0),
                "assessment_date": _today_iso(),
            })

        return {
            "report_type": ReportType.CUTOFF_COMPLIANCE.value,
            "cutoff_date": EUDR_CUTOFF_DATE.isoformat(),
            "total_plots": len(plot_ids),
            "verdicts": verdicts,
            "assessment_date": _today_iso(),
            "plots": plots_data,
        }

    def _generate_risk_assessment_report(
        self,
        plot_ids: List[str],
        analysis_results: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Generate risk assessment report data.

        Args:
            plot_ids: Plot identifiers.
            analysis_results: Analysis results dictionary.

        Returns:
            Report data dictionary.
        """
        verdicts: List[Dict[str, Any]] = []
        plots_data: List[Dict[str, Any]] = []
        risk_scores: List[float] = []

        for plot_id in plot_ids:
            plot_data = analysis_results.get(plot_id, {})
            risk_score = plot_data.get("risk_score", 0.0)
            risk_tier = plot_data.get("risk_tier", "MODERATE")
            confidence = plot_data.get("confidence", 0.0)

            risk_scores.append(risk_score)

            if risk_tier in ("CRITICAL", "HIGH"):
                verdict = "non_compliant"
            elif confidence < 0.5:
                verdict = "inconclusive"
            else:
                verdict = "compliant"

            verdicts.append({
                "plot_id": plot_id,
                "verdict": verdict,
                "confidence": confidence,
                "risk_score": risk_score,
                "risk_tier": risk_tier,
            })
            plots_data.append({
                "plot_id": plot_id,
                "verdict": verdict,
                "confidence": confidence,
                "risk_tier": risk_tier,
                "risk_score": risk_score,
                "latitude": plot_data.get("latitude", 0.0),
                "longitude": plot_data.get("longitude", 0.0),
                "area_ha": plot_data.get("area_ha", 0.0),
                "commodity": plot_data.get("commodity", ""),
                "transition_detected": plot_data.get(
                    "transition_detected", False,
                ),
                "assessment_date": _today_iso(),
            })

        tier_distribution = {
            "CRITICAL": sum(1 for s in risk_scores if s > 75.0),
            "HIGH": sum(1 for s in risk_scores if 50.0 < s <= 75.0),
            "MODERATE": sum(1 for s in risk_scores if 25.0 < s <= 50.0),
            "LOW": sum(1 for s in risk_scores if s <= 25.0),
        }

        return {
            "report_type": ReportType.RISK_ASSESSMENT.value,
            "total_plots": len(plot_ids),
            "mean_risk_score": round(
                sum(risk_scores) / max(len(risk_scores), 1), 2,
            ),
            "max_risk_score": round(
                max(risk_scores, default=0.0), 2,
            ),
            "tier_distribution": tier_distribution,
            "verdicts": verdicts,
            "assessment_date": _today_iso(),
            "plots": plots_data,
        }

