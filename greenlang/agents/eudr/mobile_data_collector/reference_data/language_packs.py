def get_label(
    category: str,
    key: str,
    language: str = "en",
) -> str:
    """Return a translated label for the given category, key, and language.

    Falls back to English if the requested language is not available,
    then to the key itself if neither is available.

    Args:
        category: Label category (common, eudr, button, status, validation).
        key: Label key within the category.
        language: ISO language code.

    Returns:
        Translated string.
    """
    category_map: Dict[str, Dict[str, Dict[str, str]]] = {
        "common": COMMON_LABELS,
        "eudr": EUDR_TERMS,
        "button": BUTTON_LABELS,
        "status": STATUS_MESSAGES,
        "validation": VALIDATION_MESSAGES,
    }
    cat = category_map.get(category, {})
    labels = cat.get(key, {})
    return labels.get(language, labels.get("en", key))


def get_all_labels_for_language(language: str = "en") -> Dict[str, Dict[str, str]]:
    """Return all labels for a given language, organized by category.

    Args:
        language: ISO language code.

    Returns:
        Nested dict: {category: {key: translated_string}}.
    """
    all_categories = {
        "common": COMMON_LABELS,
        "eudr": EUDR_TERMS,
        "button": BUTTON_LABELS,
        "status": STATUS_MESSAGES,
        "validation": VALIDATION_MESSAGES,
    }
    result: Dict[str, Dict[str, str]] = {}
    for cat_name, cat_data in all_categories.items():
        result[cat_name] = {}
        for key, translations in cat_data.items():
            result[cat_name][key] = translations.get(
                language, translations.get("en", key)
            )
    return result


def list_supported_languages() -> List[str]:
    """Return sorted list of all languages with at least one translation.

    Returns:
        Sorted list of language code strings.
    """
    langs: set[str] = set()
    for translations in COMMON_LABELS.values():
        langs.update(translations.keys())
    for translations in EUDR_TERMS.values():
        langs.update(translations.keys())
    for translations in BUTTON_LABELS.values():
        langs.update(translations.keys())
    return sorted(langs)


