def get_authority_by_member_state(
    member_state: str,
) -> Optional[Dict[str, Any]]:
    """Get competent authority profile by EU Member State code.

    Args:
        member_state: ISO 3166-1 alpha-2 country code.

    Returns:
        Authority profile dictionary or None if not found.
    """
    return EU_COMPETENT_AUTHORITIES.get(member_state.upper())


def get_all_member_states() -> List[str]:
    """Get all supported EU Member State codes.

    Returns:
        Sorted list of ISO 3166-1 alpha-2 country codes.
    """
    return sorted(EU_COMPETENT_AUTHORITIES.keys())
