"""
GreenLang Foundation Layer Agents
=================================

The Foundation Layer provides core infrastructure agents that all other
agents depend on. These 10 agents form the "operating system" of GreenLang.

Agents:
    GL-FOUND-X-001: GreenLang Orchestrator - DAG execution engine
    GL-FOUND-X-002: Schema Compiler & Validator - Input validation
    GL-FOUND-X-003: Unit & Reference Normalizer - Unit conversion
    GL-FOUND-X-004: Assumptions Registry - Assumption management
    GL-FOUND-X-005: Citations & Evidence Agent - Provenance tracking
    GL-FOUND-X-006: Access & Policy Guard - Authorization
    GL-FOUND-X-007: Versioned Agent Registry - Agent catalog
    GL-FOUND-X-008: Run Reproducibility Agent - Determinism verification
    GL-FOUND-X-009: QA Test Harness Agent - Testing framework
    GL-FOUND-X-010: Observability Agent - Metrics & monitoring
"""

