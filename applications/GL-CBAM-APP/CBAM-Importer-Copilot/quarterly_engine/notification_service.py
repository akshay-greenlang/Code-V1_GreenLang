class NotificationService:
    """
    Multi-channel notification service for CBAM quarterly report lifecycle.

    Manages notification routing, template rendering, delivery tracking,
    and audit logging across email and webhook channels.

    Thread Safety:
        Uses threading.RLock for all mutable state access.

    Example:
        >>> service = NotificationService()
        >>> service.configure_recipients("NL123", {
        ...     "email_recipients": ["compliance@acme.eu"],
        ...     "webhook_urls": ["https://hooks.slack.com/..."],
        ... })
        >>> success = service.send_deadline_notification(alert)
    """

    def __init__(self) -> None:
        """Initialize the notification service."""
        self._lock = threading.RLock()
        # importer_id -> NotificationConfig
        self._configs: Dict[str, NotificationConfig] = {}
        # Notification log: list of NotificationLogEntry
        self._notification_log: List[NotificationLogEntry] = []
        # Webhook handlers (extensible)
        self._webhook_handlers: Dict[str, Any] = {}
        logger.info("NotificationService initialized")

    # ========================================================================
    # CONFIGURATION
    # ========================================================================

    def configure_recipients(
        self,
        importer_id: str,
        config: Dict[str, Any],
    ) -> Dict[str, Any]:
        """
        Set notification routing configuration for an importer.

        Args:
            importer_id: Importer EORI or internal identifier.
            config: Configuration dict with:
                - email_recipients: list of email addresses
                - webhook_urls: list of webhook endpoints
                - alert_levels_enabled: list of AlertLevel values
                - notification_types_enabled: list of NotificationType values
                - quiet_hours_start: UTC hour (0-23)
                - quiet_hours_end: UTC hour (0-23)

        Returns:
            Stored configuration as dict.
        """
        with self._lock:
            notification_config = NotificationConfig(
                importer_id=importer_id,
                email_recipients=config.get("email_recipients", []),
                webhook_urls=config.get("webhook_urls", []),
                alert_levels_enabled=[
                    AlertLevel(level) if isinstance(level, str) else level
                    for level in config.get("alert_levels_enabled", list(AlertLevel))
                ],
                notification_types_enabled=[
                    NotificationType(nt) if isinstance(nt, str) else nt
                    for nt in config.get(
                        "notification_types_enabled", list(NotificationType)
                    )
                ],
                quiet_hours_start=config.get("quiet_hours_start"),
                quiet_hours_end=config.get("quiet_hours_end"),
            )

            self._configs[importer_id] = notification_config

            logger.info(
                "Notification config set for %s: %d email, %d webhook recipients",
                importer_id,
                len(notification_config.email_recipients),
                len(notification_config.webhook_urls),
            )

            return notification_config.model_dump()

    def get_config(self, importer_id: str) -> Optional[NotificationConfig]:
        """
        Get the notification configuration for an importer.

        Args:
            importer_id: Importer identifier.

        Returns:
            NotificationConfig if configured, None otherwise.
        """
        with self._lock:
            return self._configs.get(importer_id)

    # ========================================================================
    # DEADLINE NOTIFICATIONS
    # ========================================================================

    def send_deadline_notification(
        self,
        alert: DeadlineAlert,
    ) -> bool:
        """
        Send a deadline notification based on a DeadlineAlert.

        Routes the notification to all configured channels for the
        importer. Respects quiet hours and enabled alert levels.

        Args:
            alert: The DeadlineAlert to send notifications for.

        Returns:
            True if at least one notification was delivered successfully.
        """
        # Extract importer_id from alert recipients or use a default
        importer_id = self._resolve_importer_from_alert(alert)
        config = self._get_or_default_config(importer_id)

        # Check if this alert level is enabled
        if alert.alert_level not in config.alert_levels_enabled:
            logger.debug(
                "Alert level %s not enabled for %s, skipping",
                alert.alert_level.value, importer_id,
            )
            return False

        # Check quiet hours
        if self._is_quiet_hours(config):
            logger.info(
                "Quiet hours active for %s, deferring notification", importer_id
            )
            return False

        # Determine template
        if alert.notification_type == NotificationType.DEADLINE_OVERDUE:
            template = DEADLINE_OVERDUE_TEMPLATE
        else:
            template = DEADLINE_APPROACHING_TEMPLATE

        period = alert.report_period
        phase = "Transitional" if period.is_transitional else "Definitive"

        template_vars = {
            "subject": alert.notification_type.default_subject,
            "importer_id": importer_id,
            "period_label": period.period_label,
            "quarter_label": period.quarter.label,
            "days_remaining": max(0, alert.days_remaining),
            "days_overdue": abs(min(0, alert.days_remaining)),
            "submission_deadline": period.submission_deadline.isoformat(),
            "amendment_deadline": period.amendment_deadline.isoformat(),
            "phase": phase,
            "message": alert.message,
            "amendment_status": (
                "Open" if alert.days_remaining > -60 else "Closed"
            ),
        }

        body = template.format(**template_vars)
        subject = template_vars["subject"]

        # Send via all channels
        success = False

        # Email channel
        for recipient in config.email_recipients:
            delivered = self._send_email(
                recipient=recipient,
                subject=subject,
                body=body,
            )
            self._log_notification(
                alert_id=alert.alert_id,
                importer_id=importer_id,
                notification_type=alert.notification_type,
                channel="email",
                recipient=recipient,
                subject=subject,
                body=body,
                success=delivered,
            )
            if delivered:
                success = True

        # Webhook channel
        for webhook_url in config.webhook_urls:
            payload = self._build_webhook_payload(alert, template_vars)
            delivered = self._send_webhook(
                url=webhook_url,
                payload=payload,
            )
            self._log_notification(
                alert_id=alert.alert_id,
                importer_id=importer_id,
                notification_type=alert.notification_type,
                channel="webhook",
                recipient=webhook_url,
                subject=subject,
                body=json.dumps(payload),
                success=delivered,
            )
            if delivered:
                success = True

        return success

    # ========================================================================
    # REPORT STATUS NOTIFICATIONS
    # ========================================================================

    def send_report_status_notification(
        self,
        report: QuarterlyReport,
        status: ReportStatus,
    ) -> bool:
        """
        Send notification about a report status change (accepted/rejected).

        Args:
            report: The quarterly report.
            status: The new status.

        Returns:
            True if at least one notification was delivered.
        """
        importer_id = report.importer_id
        config = self._get_or_default_config(importer_id)

        # Determine notification type
        if status == ReportStatus.ACCEPTED:
            notification_type = NotificationType.REPORT_ACCEPTED
        elif status == ReportStatus.REJECTED:
            notification_type = NotificationType.REPORT_REJECTED
        else:
            notification_type = NotificationType.DEADLINE_APPROACHING

        if notification_type not in config.notification_types_enabled:
            return False

        # Build details based on status
        if status == ReportStatus.ACCEPTED:
            details = (
                "Your CBAM quarterly report has been accepted by the registry.\n"
                "No further action is required for this reporting period.\n"
                "Please retain all supporting documentation for potential audits."
            )
        elif status == ReportStatus.REJECTED:
            details = (
                "Your CBAM quarterly report has been rejected by the registry.\n"
                "Please review the rejection reasons and resubmit a corrected report.\n"
                f"Amendment deadline: {report.period.amendment_deadline}"
            )
        else:
            details = f"Report status changed to {status.value}."

        template_vars = {
            "subject": notification_type.default_subject,
            "importer_id": importer_id,
            "report_id": report.report_id,
            "period_label": report.period.period_label,
            "status": status.value.upper(),
            "updated_at": datetime.now(timezone.utc).isoformat(),
            "details": details,
        }

        body = REPORT_STATUS_TEMPLATE.format(**template_vars)
        subject = template_vars["subject"]

        success = False

        for recipient in config.email_recipients:
            delivered = self._send_email(recipient, subject, body)
            self._log_notification(
                alert_id=None,
                importer_id=importer_id,
                notification_type=notification_type,
                channel="email",
                recipient=recipient,
                subject=subject,
                body=body,
                success=delivered,
            )
            if delivered:
                success = True

        for webhook_url in config.webhook_urls:
            payload = {
                "event": "report_status_change",
                "report_id": report.report_id,
                "period_label": report.period.period_label,
                "status": status.value,
                "importer_id": importer_id,
                "timestamp": datetime.now(timezone.utc).isoformat(),
            }
            delivered = self._send_webhook(webhook_url, payload)
            self._log_notification(
                alert_id=None,
                importer_id=importer_id,
                notification_type=notification_type,
                channel="webhook",
                recipient=webhook_url,
                subject=subject,
                body=json.dumps(payload),
                success=delivered,
            )
            if delivered:
                success = True

        return success

    # ========================================================================
    # AMENDMENT NOTIFICATIONS
    # ========================================================================

    def send_amendment_notification(
        self,
        amendment: ReportAmendment,
    ) -> bool:
        """
        Send notification about an amendment being processed.

        Args:
            amendment: The processed amendment.

        Returns:
            True if at least one notification was delivered.
        """
        # Resolve importer from amendment.report_id
        importer_id = self._extract_importer_from_report_id(
            amendment.report_id
        )
        config = self._get_or_default_config(importer_id)

        notification_type = NotificationType.AMENDMENT_REQUIRED

        if notification_type not in config.notification_types_enabled:
            return False

        template_vars = {
            "subject": "CBAM Report Amendment Processed",
            "importer_id": importer_id,
            "report_id": amendment.report_id,
            "amendment_id": amendment.amendment_id,
            "version": amendment.version,
            "reason": amendment.reason.description,
            "amended_by": amendment.amended_by,
            "amended_at": amendment.amended_at.isoformat(),
            "changes_summary": amendment.changes_summary,
            "previous_hash": amendment.previous_hash[:16] + "...",
            "new_hash": amendment.new_hash[:16] + "...",
        }

        body = AMENDMENT_TEMPLATE.format(**template_vars)
        subject = template_vars["subject"]

        success = False

        for recipient in config.email_recipients:
            delivered = self._send_email(recipient, subject, body)
            self._log_notification(
                alert_id=None,
                importer_id=importer_id,
                notification_type=notification_type,
                channel="email",
                recipient=recipient,
                subject=subject,
                body=body,
                success=delivered,
            )
            if delivered:
                success = True

        for webhook_url in config.webhook_urls:
            payload = {
                "event": "amendment_processed",
                "amendment_id": amendment.amendment_id,
                "report_id": amendment.report_id,
                "version": amendment.version,
                "reason": amendment.reason.value,
                "amended_by": amendment.amended_by,
                "timestamp": datetime.now(timezone.utc).isoformat(),
            }
            delivered = self._send_webhook(webhook_url, payload)
            self._log_notification(
                alert_id=None,
                importer_id=importer_id,
                notification_type=notification_type,
                channel="webhook",
                recipient=webhook_url,
                subject=subject,
                body=json.dumps(payload),
                success=delivered,
            )
            if delivered:
                success = True

        return success

    # ========================================================================
    # NOTIFICATION LOG
    # ========================================================================

    def get_notification_log(
        self,
        importer_id: str,
        limit: int = 100,
    ) -> List[Dict[str, Any]]:
        """
        Get the notification history for an importer.

        Returns notifications sorted by sent_at (most recent first).

        Args:
            importer_id: Importer identifier.
            limit: Maximum number of entries to return.

        Returns:
            List of notification log entry dicts.
        """
        with self._lock:
            entries = [
                entry.model_dump()
                for entry in self._notification_log
                if entry.importer_id == importer_id
            ]
            # Sort by sent_at descending
            entries.sort(
                key=lambda e: e.get("sent_at", ""),
                reverse=True,
            )
            return entries[:limit]

    def get_notification_stats(
        self,
        importer_id: str,
    ) -> Dict[str, Any]:
        """
        Get notification delivery statistics for an importer.

        Args:
            importer_id: Importer identifier.

        Returns:
            Dict with delivery statistics.
        """
        with self._lock:
            entries = [
                e for e in self._notification_log
                if e.importer_id == importer_id
            ]

            total = len(entries)
            sent = sum(1 for e in entries if e.delivery_status == "sent")
            failed = sum(1 for e in entries if e.delivery_status == "failed")

            by_channel: Dict[str, int] = {}
            by_type: Dict[str, int] = {}
            for e in entries:
                by_channel[e.channel] = by_channel.get(e.channel, 0) + 1
                by_type[e.notification_type.value] = (
                    by_type.get(e.notification_type.value, 0) + 1
                )

            return {
                "total_notifications": total,
                "sent": sent,
                "failed": failed,
                "success_rate_pct": (
                    round(sent / total * 100, 1) if total > 0 else 0
                ),
                "by_channel": by_channel,
                "by_type": by_type,
            }

