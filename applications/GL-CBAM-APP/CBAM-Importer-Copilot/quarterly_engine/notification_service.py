    def _build_webhook_payload(
        self,
        alert: DeadlineAlert,
        template_vars: Dict[str, Any],
    ) -> Dict[str, Any]:
        """
        Build a structured webhook payload from an alert.

        Args:
            alert: The deadline alert.
            template_vars: Template variables for context.

        Returns:
            Structured payload dict for webhook delivery.
        """
        return {
            "event": "deadline_alert",
            "alert_id": alert.alert_id,
            "alert_level": alert.alert_level.value,
            "notification_type": alert.notification_type.value,
            "period_label": template_vars.get("period_label", ""),
            "days_remaining": alert.days_remaining,
            "submission_deadline": template_vars.get("submission_deadline", ""),
            "amendment_deadline": template_vars.get("amendment_deadline", ""),
            "message": alert.message,
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "source": "GreenLang CBAM Importer Copilot v1.1.0",
        }

    # ========================================================================
    # INTERNAL HELPERS
    # ========================================================================

    def _log_notification(
        self,
        alert_id: Optional[str],
        importer_id: str,
        notification_type: NotificationType,
        channel: str,
        recipient: str,
        subject: str,
        body: str,
        success: bool,
        error_message: Optional[str] = None,
    ) -> None:
        """
        Log a notification delivery for audit trail.

        Args:
            alert_id: Related alert ID.
            importer_id: Importer identifier.
            notification_type: Type of notification.
            channel: Delivery channel.
            recipient: Recipient address/URL.
            subject: Notification subject.
            body: Notification body.
            success: Whether delivery was successful.
            error_message: Error details if failed.
        """
        log_entry = NotificationLogEntry(
            log_id=f"LOG-{uuid.uuid4().hex[:12].upper()}",
            alert_id=alert_id,
            importer_id=importer_id,
            notification_type=notification_type,
            channel=channel,
            recipient=recipient,
            subject=subject,
            body=body,
            sent_at=datetime.now(timezone.utc),
            delivery_status="sent" if success else "failed",
            error_message=error_message,
        )

        with self._lock:
            self._notification_log.append(log_entry)

    def _get_or_default_config(
        self,
        importer_id: str,
    ) -> NotificationConfig:
        """
        Get notification config for an importer, or return a default.

        Args:
            importer_id: Importer identifier.

        Returns:
            NotificationConfig for the importer.
        """
        with self._lock:
            config = self._configs.get(importer_id)
            if config:
                return config

            # Default config with generic recipient
            return NotificationConfig(
                importer_id=importer_id,
                email_recipients=[f"{importer_id}@notifications.cbam"],
                webhook_urls=[],
            )

    def _resolve_importer_from_alert(self, alert: DeadlineAlert) -> str:
        """
        Extract the importer ID from an alert's recipients or metadata.

        Args:
            alert: The deadline alert.

        Returns:
            Importer identifier string.
        """
        if alert.recipients:
            # Try to extract from email format: importer_id@...
            first_recipient = alert.recipients[0]
            if "@" in first_recipient:
                return first_recipient.split("@")[0]
            return first_recipient
        return "unknown"

    def _extract_importer_from_report_id(self, report_id: str) -> str:
        """
        Extract the importer identifier from a report ID.

        Report ID format: CBAM-QR-{period}-{importer}-{timestamp}

        Args:
            report_id: The report identifier.

        Returns:
            Extracted importer identifier, or "unknown".
        """
        parts = report_id.split("-")
        if len(parts) >= 4:
            # The importer part is between period and timestamp
            # CBAM-QR-2025Q4-NL123456789012-20260115120000
            return parts[3] if len(parts) > 3 else "unknown"
        return "unknown"

    def _is_quiet_hours(self, config: NotificationConfig) -> bool:
        """
        Check if the current UTC time falls within quiet hours.

        Args:
            config: Notification configuration with quiet hours settings.

        Returns:
            True if currently in quiet hours.
        """
        if config.quiet_hours_start is None or config.quiet_hours_end is None:
            return False

        current_hour = datetime.now(timezone.utc).hour
        start = config.quiet_hours_start
        end = config.quiet_hours_end

        if start <= end:
            return start <= current_hour < end
        else:
            # Wraps past midnight (e.g., 22:00 - 06:00)
            return current_hour >= start or current_hour < end
