def compute_sha256(data: Union[str, bytes, dict]) -> str:
    """
    Compute SHA-256 hash of arbitrary data.

    This is a ZERO-HALLUCINATION deterministic computation.

    Args:
        data: String, bytes, or dict to hash. Dicts are JSON-serialized
              with sorted keys for deterministic output.

    Returns:
        Hexadecimal SHA-256 hash string (64 characters).
    """
    if isinstance(data, dict):
        data = json.dumps(data, sort_keys=True, default=str)
    if isinstance(data, str):
        data = data.encode("utf-8")
    return hashlib.sha256(data).hexdigest()


def quantize_decimal(
    value: Union[Decimal, float, str],
    places: int = 3
) -> Decimal:
    """
    Quantize a value to the specified number of decimal places using ROUND_HALF_UP.

    This is the standard rounding method for CBAM regulatory calculations
    to ensure consistency across implementations.

    Args:
        value: The value to quantize.
        places: Number of decimal places (default: 3 for metric tonnes).

    Returns:
        Quantized Decimal value.
    """
    if not isinstance(value, Decimal):
        value = Decimal(str(value))
    quantizer = Decimal(10) ** -places
    return value.quantize(quantizer, rounding=ROUND_HALF_UP)


def validate_status_transition(
    current: ReportStatus,
    target: ReportStatus
) -> bool:
    """
    Validate that a status transition is allowed.

    Args:
        current: Current report status.
        target: Target report status.

    Returns:
        True if the transition is valid.

    Raises:
        ValueError: If the transition is not allowed.
    """
    allowed = VALID_STATUS_TRANSITIONS.get(current, [])
    if target not in allowed:
        raise ValueError(
            f"Invalid status transition: {current.value} -> {target.value}. "
            f"Allowed transitions from {current.value}: "
            f"{[s.value for s in allowed]}"
        )
    return True
