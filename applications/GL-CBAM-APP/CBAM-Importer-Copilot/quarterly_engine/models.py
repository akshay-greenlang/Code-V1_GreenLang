class QuarterlyPeriod(str, Enum):
    """CBAM quarterly reporting periods per Regulation 2023/956 Article 6."""

    Q1 = "Q1"
    Q2 = "Q2"
    Q3 = "Q3"
    Q4 = "Q4"

    @property
    def start_month(self) -> int:
        """Return the first month of this quarter (1-indexed)."""
        return QUARTER_MONTHS[self][0]

    @property
    def end_month(self) -> int:
        """Return the last month of this quarter (1-indexed)."""
        return QUARTER_MONTHS[self][2]

    @property
    def label(self) -> str:
        """Human-readable label for the quarter."""
        labels = {
            QuarterlyPeriod.Q1: "January - March",
            QuarterlyPeriod.Q2: "April - June",
            QuarterlyPeriod.Q3: "July - September",
            QuarterlyPeriod.Q4: "October - December",
        }
        return labels[self]


class ReportStatus(str, Enum):
    """
    Report lifecycle status per Implementing Regulation 2023/1773 Article 8.

    Lifecycle: draft -> in_review -> submitted -> accepted/rejected -> amended
    """

    DRAFT = "draft"
    IN_REVIEW = "in_review"
    SUBMITTED = "submitted"
    ACCEPTED = "accepted"
    REJECTED = "rejected"
    AMENDED = "amended"

    @property
    def is_terminal(self) -> bool:
        """Whether this status represents a terminal state."""
        return self in {ReportStatus.ACCEPTED}

    @property
    def is_editable(self) -> bool:
        """Whether the report can be modified in this status."""
        return self in {ReportStatus.DRAFT, ReportStatus.REJECTED, ReportStatus.AMENDED}

    @property
    def allows_amendment(self) -> bool:
        """Whether an amendment can be created from this status."""
        return self in {ReportStatus.SUBMITTED, ReportStatus.ACCEPTED, ReportStatus.REJECTED}


