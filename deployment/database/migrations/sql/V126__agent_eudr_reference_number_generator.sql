-- ============================================================================
-- 1. gl_eudr_rng_reference_numbers -- Main reference number registry
-- ============================================================================
-- Stores every reference number generated by the platform. Each reference
-- number uniquely identifies an EUDR Due Diligence Statement and follows
-- the format: {MemberStateCode}-{YYYY}-{OperatorPrefix}-{SequenceNumber}-
-- {CheckDigit}. The reference number is immutable once assigned to a DDS
-- and must be retained for a minimum of 10 years per EUDR Article 12.
-- Reference numbers may be in one of five lifecycle states: active (just
-- generated, available for assignment), used (assigned to a DDS), expired
-- (past validity window without assignment), revoked (withdrawn due to
-- error or fraud), or transferred (ownership moved to another operator).
-- ============================================================================
RAISE NOTICE 'V126 [1/9]: Creating gl_eudr_rng_reference_numbers...';

CREATE TABLE IF NOT EXISTS gl_eudr_rng_reference_numbers (
    ref_number_id                   UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
        -- Unique identifier for this reference number record
    reference_number                VARCHAR(100)    UNIQUE NOT NULL,
        -- The generated reference number string following the member-state format
        -- e.g. "DE-2026-OPR00123-000457-K" or "FR-2026-SA789-001234-7"
    member_state_code               VARCHAR(5)      NOT NULL,
        -- ISO 3166-1 alpha-2 code of the EU member state (e.g. "DE", "FR", "IT")
    year                            INTEGER         NOT NULL,
        -- Generation year (four-digit, e.g. 2026)
    operator_id                     VARCHAR(100)    NOT NULL,
        -- EUDR operator or trader identifier to whom this reference number belongs
    tenant_id                       VARCHAR(100)    NOT NULL DEFAULT '',
        -- Multi-tenant isolation identifier
    sequence_number                 BIGINT          NOT NULL,
        -- Sequential number within the (member_state_code, year, operator_id) scope
    checksum                        VARCHAR(10)     NOT NULL,
        -- Check digit(s) computed via Luhn-mod-36 or member-state-specific algorithm
    format_version                  VARCHAR(10)     NOT NULL DEFAULT '1.0',
        -- Version of the reference number format specification applied
    status                          VARCHAR(20)     NOT NULL DEFAULT 'active',
        -- Current lifecycle status of this reference number
    generated_at                    TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
        -- Timestamp when this reference number was generated
    expires_at                      TIMESTAMPTZ,
        -- Expiration timestamp after which an unassigned reference number becomes expired
        -- Typically 90 days from generation for unassigned numbers
    activated_at                    TIMESTAMPTZ,
        -- Timestamp when the reference number was first activated (assigned to a DDS)
    revoked_at                      TIMESTAMPTZ,
        -- Timestamp when the reference number was revoked (NULL if not revoked)
    revocation_reason               TEXT,
        -- Reason for revocation: regulatory requirement, operator request, fraud, error
    associated_dds_id               UUID,
        -- FK reference to the Due Diligence Statement that uses this reference number
        -- NULL when status is 'active' (not yet assigned) or 'expired'
    associated_dds_number           VARCHAR(100),
        -- Denormalized DDS number for query performance
    commodity                       VARCHAR(50),
        -- EUDR commodity associated with the DDS (denormalized)
    origin_country                  VARCHAR(5),
        -- Country of production associated with the DDS (denormalized)
    batch_id                        UUID,
        -- FK reference to the batch request that generated this number (NULL if single)
    operator_prefix                 VARCHAR(50),
        -- Operator-specific prefix embedded in the reference number
    generation_method               VARCHAR(30)     NOT NULL DEFAULT 'sequential',
        -- Method used to generate this reference number
    verification_count              INTEGER         NOT NULL DEFAULT 0,
        -- Number of times this reference number has been verified/validated
    last_verified_at                TIMESTAMPTZ,
        -- Timestamp of the most recent verification
    metadata                        JSONB           DEFAULT '{}',
        -- Additional metadata: {"eu_is_submitted": true, "submission_ref": "...", "notes": "..."}
    tags                            JSONB           DEFAULT '[]',
        -- Organizational tags for filtering
    created_by                      VARCHAR(100)    NOT NULL DEFAULT 'system',
        -- User or system that generated this reference number
    provenance_hash                 VARCHAR(64),
        -- SHA-256 hash for reference number record integrity verification
    created_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT chk_rng_ref_status CHECK (status IN (
        'active', 'used', 'expired', 'revoked', 'transferred'
    )),
    CONSTRAINT chk_rng_ref_year CHECK (year >= 2023 AND year <= 2099),
    CONSTRAINT chk_rng_ref_seq CHECK (sequence_number >= 1),
    CONSTRAINT chk_rng_ref_method CHECK (generation_method IN (
        'sequential', 'batch', 'pre_allocated', 'emergency', 'migrated'
    )),
    CONSTRAINT chk_rng_ref_commodity CHECK (commodity IS NULL OR commodity IN (
        'cattle', 'cocoa', 'coffee', 'oil_palm', 'rubber', 'soya', 'wood'
    )),
    CONSTRAINT chk_rng_ref_verify_count CHECK (verification_count >= 0),
    CONSTRAINT chk_rng_ref_expires_after_gen CHECK (expires_at IS NULL OR expires_at > generated_at),
    CONSTRAINT chk_rng_ref_revoked_consistency CHECK (
        (status = 'revoked' AND revoked_at IS NOT NULL AND revocation_reason IS NOT NULL)
        OR (status != 'revoked')
    ),
    CONSTRAINT chk_rng_ref_used_consistency CHECK (
        (status = 'used' AND associated_dds_id IS NOT NULL)
        OR (status != 'used')
    )
);

