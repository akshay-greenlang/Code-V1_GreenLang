-- Function: Compute Luhn-mod-36 checksum for a reference number prefix
-- Implements the Luhn mod N algorithm (https://en.wikipedia.org/wiki/Luhn_mod_N_algorithm)
-- with N=36 (digits 0-9 + letters A-Z) to produce a single check character.
-- The check digit enables client-side validation without a database lookup.
CREATE OR REPLACE FUNCTION fn_eudr_rng_compute_checksum(
    p_input VARCHAR,
    p_algorithm VARCHAR DEFAULT 'luhn_mod36'
)
RETURNS VARCHAR
LANGUAGE plpgsql
AS $$
DECLARE
    v_charset       VARCHAR := '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    v_n             INTEGER := 36;  -- Base for mod-36
    v_factor        INTEGER := 2;
    v_sum           INTEGER := 0;
    v_i             INTEGER;
    v_code_point    INTEGER;
    v_addend        INTEGER;
    v_input_upper   VARCHAR;
    v_char          VARCHAR(1);
BEGIN
    IF p_algorithm = 'luhn_mod36' THEN
        -- Luhn mod 36 algorithm
        v_input_upper := UPPER(REPLACE(REPLACE(p_input, '-', ''), ' ', ''));

        FOR v_i IN REVERSE LENGTH(v_input_upper)..1 LOOP
            v_char := SUBSTRING(v_input_upper FROM v_i FOR 1);
            v_code_point := POSITION(v_char IN v_charset) - 1;

            IF v_code_point < 0 THEN
                RAISE EXCEPTION 'Invalid character "%" in input for Luhn-mod-36', v_char;
            END IF;

            v_addend := v_factor * v_code_point;

            -- Factor alternates between 1 and 2
            v_factor := CASE WHEN v_factor = 2 THEN 1 ELSE 2 END;

            -- Sum the digits of the addend in base N
            v_addend := (v_addend / v_n) + (v_addend % v_n);
            v_sum := v_sum + v_addend;
        END LOOP;

        -- The check character is the character that makes the total sum divisible by N
        RETURN SUBSTRING(v_charset FROM ((v_n - (v_sum % v_n)) % v_n) + 1 FOR 1);

    ELSIF p_algorithm = 'luhn_mod10' THEN
        -- Standard Luhn mod 10 (numeric only)
        v_input_upper := REPLACE(REPLACE(p_input, '-', ''), ' ', '');
        v_factor := 2;
        v_sum := 0;

        FOR v_i IN REVERSE LENGTH(v_input_upper)..1 LOOP
            v_code_point := CAST(SUBSTRING(v_input_upper FROM v_i FOR 1) AS INTEGER);
            v_addend := v_factor * v_code_point;
            v_factor := CASE WHEN v_factor = 2 THEN 1 ELSE 2 END;
            v_addend := (v_addend / 10) + (v_addend % 10);
            v_sum := v_sum + v_addend;
        END LOOP;

        RETURN CAST((10 - (v_sum % 10)) % 10 AS VARCHAR);

    ELSIF p_algorithm = 'none' THEN
        RETURN '';

    ELSE
        RAISE EXCEPTION 'Unsupported checksum algorithm: %', p_algorithm;
    END IF;
END;
$$;

COMMENT ON FUNCTION fn_eudr_rng_compute_checksum IS 'AGENT-EUDR-038: Computes check digit(s) for a reference number prefix using Luhn-mod-36 (alphanumeric, default), Luhn-mod-10 (numeric), or no checksum. Enables client-side format validation without database lookup';


-- Function: Validate a complete reference number (format + checksum + existence)
CREATE OR REPLACE FUNCTION fn_eudr_rng_validate_reference(
    p_reference_number VARCHAR
)
RETURNS TABLE (
    is_valid            BOOLEAN,
    format_valid        BOOLEAN,
    checksum_valid      BOOLEAN,
    exists_in_registry  BOOLEAN,
    current_status      VARCHAR(20),
    operator_id         VARCHAR(100),
    member_state_code   VARCHAR(5),
    year                INTEGER,
    error_code          VARCHAR(30),
    error_message       TEXT
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_ref               RECORD;
    v_rule              RECORD;
    v_format_ok         BOOLEAN := FALSE;
    v_checksum_ok       BOOLEAN := FALSE;
    v_exists            BOOLEAN := FALSE;
    v_parts             TEXT[];
    v_prefix            TEXT;
    v_expected_checksum VARCHAR;
    v_actual_checksum   VARCHAR;
    v_ms_code           VARCHAR(5);
    v_year_str          VARCHAR;
    v_error_code        VARCHAR(30);
    v_error_msg         TEXT;
BEGIN
    -- Step 1: Basic format validation (split by separator)
    v_parts := string_to_array(p_reference_number, '-');

    IF array_length(v_parts, 1) < 4 THEN
        RETURN QUERY SELECT
            FALSE, FALSE, FALSE, FALSE,
            NULL::VARCHAR(20), NULL::VARCHAR(100), NULL::VARCHAR(5), NULL::INTEGER,
            'INVALID_FORMAT'::VARCHAR(30), 'Reference number must have at least 4 hyphen-separated segments'::TEXT;
        RETURN;
    END IF;

    -- Extract member state code and year
    v_ms_code := v_parts[1];
    v_year_str := v_parts[2];

    -- Validate member state (2-letter uppercase)
    IF LENGTH(v_ms_code) != 2 OR v_ms_code !~ '^[A-Z]{2}$' THEN
        RETURN QUERY SELECT
            FALSE, FALSE, FALSE, FALSE,
            NULL::VARCHAR(20), NULL::VARCHAR(100), v_ms_code, NULL::INTEGER,
            'INVALID_MEMBER_STATE'::VARCHAR(30), 'Invalid member state code: must be 2 uppercase letters'::TEXT;
        RETURN;
    END IF;

    -- Validate year (4-digit)
    IF v_year_str !~ '^\d{4}$' THEN
        RETURN QUERY SELECT
            FALSE, FALSE, FALSE, FALSE,
            NULL::VARCHAR(20), NULL::VARCHAR(100), v_ms_code, NULL::INTEGER,
            'INVALID_YEAR'::VARCHAR(30), 'Invalid year: must be 4 digits'::TEXT;
        RETURN;
    END IF;

    v_format_ok := TRUE;

    -- Step 2: Checksum validation
    -- The last segment is the checksum; everything before it is the prefix
    v_actual_checksum := v_parts[array_length(v_parts, 1)];
    v_prefix := array_to_string(v_parts[1:array_length(v_parts, 1) - 1], '-');

    -- Look up the applicable format rule for checksum algorithm
    SELECT fr.* INTO v_rule
    FROM gl_eudr_rng_format_rules fr
    WHERE fr.member_state_code = v_ms_code
      AND fr.active = TRUE
      AND (fr.effective_to IS NULL OR fr.effective_to >= CURRENT_DATE)
    ORDER BY fr.effective_from DESC
    LIMIT 1;

    IF v_rule IS NOT NULL THEN
        -- Compute expected checksum
        v_expected_checksum := fn_eudr_rng_compute_checksum(v_prefix, v_rule.checksum_algorithm);
        v_checksum_ok := (UPPER(v_actual_checksum) = UPPER(v_expected_checksum));

        -- Also validate against regex if available
        IF v_rule.regex_pattern IS NOT NULL THEN
            v_format_ok := v_format_ok AND (p_reference_number ~ v_rule.regex_pattern);
        END IF;
    ELSE
        -- No rule found; use default luhn_mod36
        v_expected_checksum := fn_eudr_rng_compute_checksum(v_prefix, 'luhn_mod36');
        v_checksum_ok := (UPPER(v_actual_checksum) = UPPER(v_expected_checksum));
    END IF;

    -- Step 3: Registry lookup
    SELECT rn.* INTO v_ref
    FROM gl_eudr_rng_reference_numbers rn
    WHERE rn.reference_number = p_reference_number;

    v_exists := FOUND;

    -- Determine error if any
    IF NOT v_format_ok THEN
        v_error_code := 'INVALID_FORMAT';
        v_error_msg := 'Reference number format does not match expected pattern';
    ELSIF NOT v_checksum_ok THEN
        v_error_code := 'INVALID_CHECKSUM';
        v_error_msg := format('Checksum mismatch: expected %s, got %s', v_expected_checksum, v_actual_checksum);
    ELSIF NOT v_exists THEN
        v_error_code := 'NOT_FOUND';
        v_error_msg := 'Reference number not found in registry';
    ELSIF v_ref.status = 'expired' THEN
        v_error_code := 'EXPIRED';
        v_error_msg := 'Reference number has expired';
    ELSIF v_ref.status = 'revoked' THEN
        v_error_code := 'REVOKED';
        v_error_msg := format('Reference number was revoked: %s', COALESCE(v_ref.revocation_reason, 'no reason'));
    ELSIF v_ref.status = 'transferred' THEN
        v_error_code := 'TRANSFERRED';
        v_error_msg := 'Reference number has been transferred to another operator';
    END IF;

    -- Update verification count if found
    IF v_exists THEN
        UPDATE gl_eudr_rng_reference_numbers
        SET verification_count = verification_count + 1,
            last_verified_at = NOW(),
            updated_at = NOW()
        WHERE ref_number_id = v_ref.ref_number_id;
    END IF;

    RETURN QUERY SELECT
        (v_format_ok AND v_checksum_ok AND v_exists AND v_ref.status IN ('active', 'used')),
        v_format_ok,
        v_checksum_ok,
        v_exists,
        CASE WHEN v_exists THEN v_ref.status ELSE NULL END,
        CASE WHEN v_exists THEN v_ref.operator_id ELSE NULL END,
        v_ms_code,
        CAST(v_year_str AS INTEGER),
        v_error_code,
        v_error_msg;
END;
$$;

COMMENT ON FUNCTION fn_eudr_rng_validate_reference IS 'AGENT-EUDR-038: Full reference number validation: format parsing, Luhn-mod-36 checksum verification, registry existence check, status validation. Returns detailed validation result with error codes for compliance audit per EUDR Articles 12, 31';


-- Function: Bulk expire reference numbers past their expiration date
CREATE OR REPLACE FUNCTION fn_eudr_rng_expire_stale_references()
RETURNS TABLE (
    expired_count   INTEGER,
    operator_counts JSONB
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_count         INTEGER;
    v_op_counts     JSONB;
BEGIN
    -- Expire all active reference numbers past their expiration date
    WITH expired AS (
        UPDATE gl_eudr_rng_reference_numbers
        SET status = 'expired',
            updated_at = NOW()
        WHERE status = 'active'
          AND expires_at IS NOT NULL
          AND expires_at < NOW()
        RETURNING operator_id
    )
    SELECT COUNT(*), COALESCE(jsonb_object_agg(op, cnt), '{}'::JSONB)
    INTO v_count, v_op_counts
    FROM (
        SELECT operator_id AS op, COUNT(*) AS cnt
        FROM expired
        GROUP BY operator_id
    ) sub;

    RETURN QUERY SELECT v_count, COALESCE(v_op_counts, '{}'::JSONB);
END;
$$;

COMMENT ON FUNCTION fn_eudr_rng_expire_stale_references IS 'AGENT-EUDR-038: Bulk expires active reference numbers past their expiration date. Returns count of expired numbers and per-operator breakdown for notification and cleanup scheduling';


