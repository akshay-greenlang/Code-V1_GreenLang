-- ============================================================================
-- FUNCTIONS: Atomic sequence generation and checksum computation
-- ============================================================================
RAISE NOTICE 'V126: Creating helper functions...';

-- Function: Atomic next-sequence allocation with row-level locking
-- This is the core concurrency-safe function that prevents duplicate sequence
-- allocation even under heavy concurrent load. It uses SELECT ... FOR UPDATE
-- to acquire an exclusive row-level lock on the counter, increments the
-- sequence, and returns the new value. If no counter exists for the given
-- (member_state_code, year, operator_id) tuple, it creates one atomically.
CREATE OR REPLACE FUNCTION fn_eudr_rng_next_sequence(
    p_member_state_code VARCHAR(5),
    p_year INTEGER,
    p_operator_id VARCHAR(100),
    p_count INTEGER DEFAULT 1
)
RETURNS TABLE (
    sequence_start  BIGINT,
    sequence_end    BIGINT,
    remaining       BIGINT,
    exhaustion_pct  NUMERIC(7,4)
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_counter       RECORD;
    v_new_seq       BIGINT;
    v_start_seq     BIGINT;
BEGIN
    -- Acquire exclusive lock on the counter row (or create if not exists)
    -- INSERT ... ON CONFLICT ensures atomic creation of new counters
    INSERT INTO gl_eudr_rng_sequence_counters (
        member_state_code, year, operator_id, current_sequence, last_generated_at
    )
    VALUES (
        p_member_state_code, p_year, p_operator_id, 0, NOW()
    )
    ON CONFLICT (member_state_code, year, operator_id) DO NOTHING;

    -- Now lock and read the counter with FOR UPDATE (row-level exclusive lock)
    SELECT sc.* INTO v_counter
    FROM gl_eudr_rng_sequence_counters sc
    WHERE sc.member_state_code = p_member_state_code
      AND sc.year = p_year
      AND sc.operator_id = p_operator_id
    FOR UPDATE;

    -- Check if counter is locked
    IF v_counter.is_locked THEN
        RAISE EXCEPTION 'Sequence counter is locked for %-%-%: %',
            p_member_state_code, p_year, p_operator_id, COALESCE(v_counter.locked_reason, 'no reason');
    END IF;

    -- Check if we have enough capacity
    v_start_seq := v_counter.current_sequence + 1;
    v_new_seq := v_counter.current_sequence + p_count;

    IF v_new_seq > v_counter.max_sequence THEN
        -- Handle based on rollover policy
        IF v_counter.rollover_policy = 'error' OR v_counter.rollover_policy = 'notify_and_error' THEN
            RAISE EXCEPTION 'Sequence exhausted for %-%-%: current=%, max=%, requested=%',
                p_member_state_code, p_year, p_operator_id,
                v_counter.current_sequence, v_counter.max_sequence, p_count;
        ELSIF v_counter.rollover_policy = 'reset' THEN
            -- Reset to start (dangerous: may cause collisions with existing numbers)
            v_start_seq := 1;
            v_new_seq := p_count;
        ELSIF v_counter.rollover_policy = 'extend' THEN
            -- Extend max by 10x current max
            UPDATE gl_eudr_rng_sequence_counters
            SET max_sequence = max_sequence * 10,
                updated_at = NOW()
            WHERE counter_id = v_counter.counter_id;
        END IF;
    END IF;

    -- Atomically increment the counter
    UPDATE gl_eudr_rng_sequence_counters
    SET current_sequence = v_new_seq,
        last_generated_at = NOW(),
        updated_at = NOW()
    WHERE counter_id = v_counter.counter_id;

    -- Return the allocated range
    RETURN QUERY SELECT
        v_start_seq,
        v_new_seq,
        v_counter.max_sequence - v_new_seq,
        CASE
            WHEN v_counter.max_sequence = 0 THEN 0::NUMERIC(7,4)
            ELSE ROUND((v_new_seq::NUMERIC / v_counter.max_sequence::NUMERIC) * 100, 4)
        END;
END;
$$;

