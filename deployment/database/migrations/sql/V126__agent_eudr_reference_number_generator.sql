-- ============================================================================
-- 2. gl_eudr_rng_sequence_counters -- Atomic sequence management
-- ============================================================================
-- Manages atomic sequence counters per (member_state_code, year, operator_id)
-- tuple. Each counter tracks the current highest allocated sequence number
-- and the configured maximum. The fn_eudr_rng_next_sequence function uses
-- SELECT ... FOR UPDATE on this table to guarantee atomic increment under
-- concurrent load, preventing duplicate sequence allocation even with
-- multiple application instances generating reference numbers simultaneously.
-- ============================================================================
RAISE NOTICE 'V126 [2/9]: Creating gl_eudr_rng_sequence_counters...';

CREATE TABLE IF NOT EXISTS gl_eudr_rng_sequence_counters (
    counter_id                      UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
        -- Unique identifier for this sequence counter
    member_state_code               VARCHAR(5)      NOT NULL,
        -- ISO 3166-1 alpha-2 code of the EU member state
    year                            INTEGER         NOT NULL,
        -- Year for which this counter applies (four-digit)
    operator_id                     VARCHAR(100)    NOT NULL,
        -- EUDR operator or trader identifier
    tenant_id                       VARCHAR(100)    NOT NULL DEFAULT '',
        -- Multi-tenant isolation identifier
    current_sequence                BIGINT          NOT NULL DEFAULT 0,
        -- Current highest allocated sequence number (0 = no numbers generated yet)
    max_sequence                    BIGINT          NOT NULL DEFAULT 999999999,
        -- Maximum allowed sequence number for this counter
        -- Default ~1 billion per operator per year; can be restricted per format rule
    reserved_start                  BIGINT,
        -- Start of reserved sequence range (for pre-allocation)
    reserved_end                    BIGINT,
        -- End of reserved sequence range (for pre-allocation)
    allocation_block_size           INTEGER         NOT NULL DEFAULT 1,
        -- Number of sequences to allocate per atomic increment (1 = single, N = batch)
    rollover_policy                 VARCHAR(20)     NOT NULL DEFAULT 'error',
        -- What happens when current_sequence reaches max_sequence
    exhaustion_warning_threshold    NUMERIC(5,4)    NOT NULL DEFAULT 0.9000,
        -- Percentage of max_sequence at which to emit a warning (0.9000 = 90%)
    is_locked                       BOOLEAN         NOT NULL DEFAULT FALSE,
        -- Whether this counter is temporarily locked (e.g. during migration)
    locked_reason                   VARCHAR(200),
        -- Reason for locking the counter
    locked_at                       TIMESTAMPTZ,
        -- Timestamp when the counter was locked
    locked_by                       VARCHAR(100),
        -- User or system that locked the counter
    last_generated_at               TIMESTAMPTZ,
        -- Timestamp of the most recent sequence allocation
    reset_count                     INTEGER         NOT NULL DEFAULT 0,
        -- Number of times this counter has been administratively reset
    last_reset_at                   TIMESTAMPTZ,
        -- Timestamp of the most recent administrative reset
    last_reset_by                   VARCHAR(100),
        -- User who performed the most recent reset
    metadata                        JSONB           DEFAULT '{}',
        -- Additional metadata: {"notes": "...", "migration_source": "..."}
    created_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_rng_counter_ms_year_op UNIQUE (member_state_code, year, operator_id),
    CONSTRAINT chk_rng_counter_year CHECK (year >= 2023 AND year <= 2099),
    CONSTRAINT chk_rng_counter_seq CHECK (current_sequence >= 0),
    CONSTRAINT chk_rng_counter_max CHECK (max_sequence >= 1),
    CONSTRAINT chk_rng_counter_seq_max CHECK (current_sequence <= max_sequence),
    CONSTRAINT chk_rng_counter_block CHECK (allocation_block_size >= 1 AND allocation_block_size <= 10000),
    CONSTRAINT chk_rng_counter_rollover CHECK (rollover_policy IN (
        'error', 'reset', 'extend', 'notify_and_error'
    )),
    CONSTRAINT chk_rng_counter_threshold CHECK (exhaustion_warning_threshold >= 0 AND exhaustion_warning_threshold <= 1),
    CONSTRAINT chk_rng_counter_reserved CHECK (
        (reserved_start IS NULL AND reserved_end IS NULL)
        OR (reserved_start IS NOT NULL AND reserved_end IS NOT NULL AND reserved_end >= reserved_start)
    ),
    CONSTRAINT chk_rng_counter_reset CHECK (reset_count >= 0)
);

COMMENT ON TABLE gl_eudr_rng_sequence_counters IS 'AGENT-EUDR-038: Atomic sequence counters per (member_state, year, operator) with row-level locking for concurrent-safe sequence allocation, configurable max limits, pre-allocation ranges, exhaustion warnings, and rollover policies';
COMMENT ON COLUMN gl_eudr_rng_sequence_counters.current_sequence IS 'Highest allocated sequence: incremented atomically via fn_eudr_rng_next_sequence using SELECT ... FOR UPDATE. Never decremented except by administrative reset';
