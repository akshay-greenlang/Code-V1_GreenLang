class NetZeroGapEngine:
    """Gap-to-net-zero analysis engine.

    Compares current emissions against the target pathway and provides:
    - Year-by-year gap analysis (actual vs. expected)
    - Per-scope gap breakdown
    - RAG status (on-track / at-risk / off-track)
    - Linear projection of future emissions
    - Cumulative emissions budget analysis
    - Required acceleration rate
    - Time-to-target estimation

    All calculations use Decimal arithmetic for bit-perfect
    reproducibility.  No LLM is used in any calculation path.

    Usage::

        engine = NetZeroGapEngine()
        result = engine.calculate(gap_input)
        assert result.trajectory_status in ('on_track', 'at_risk', 'off_track')
    """

    engine_version: str = _MODULE_VERSION

    # ------------------------------------------------------------------ #
    # Public API                                                          #
    # ------------------------------------------------------------------ #

    def calculate(self, data: GapInput) -> GapResult:
        """Run the complete gap-to-net-zero analysis.

        Args:
            data: Validated gap analysis input.

        Returns:
            GapResult with comprehensive trajectory assessment.
        """
        t0 = time.perf_counter()
        logger.info(
            "Gap analysis: entity=%s, current_year=%d, base=%d, target=%d",
            data.entity_name, data.current_year, data.base_year,
            data.target_year,
        )

        # Step 1: Year-by-year gap
        gap_by_year = self._calculate_yearly_gaps(data)

        # Step 2: Scope-level gap
        gap_by_scope = self._calculate_scope_gaps(data)

        # Step 3: Current year gap
        current_expected = self._expected_at_year(
            data.base_year_total_tco2e,
            data.annual_rate_pct,
            data.base_year,
            data.current_year,
        )
        current_gap = _round_val(data.current_total_tco2e - current_expected)
        current_gap_pct = _round_val(
            _safe_pct(current_gap, current_expected), 2
        )

        # Step 4: Overall trajectory status
        trajectory_status = self._assess_trajectory_status(
            current_gap_pct
        )

        # Step 5: Budget analysis
        budget_start = data.budget_start_year or data.current_year
        budget = self._calculate_budget(data, budget_start)

        # Step 6: Risk assessment
        risk = self._assess_risk(data, current_gap, current_gap_pct)

        elapsed_ms = _round3((time.perf_counter() - t0) * 1000.0)

        result = GapResult(
            entity_name=data.entity_name,
            current_year=data.current_year,
            base_year=data.base_year,
            target_year=data.target_year,
            gap_by_year=gap_by_year,
            gap_by_scope=gap_by_scope,
            trajectory_status=trajectory_status,
            current_gap_tco2e=current_gap,
            current_gap_pct=current_gap_pct,
            budget_analysis=budget,
            risk_assessment=risk,
            projection_method=data.projection_method.value,
            processing_time_ms=elapsed_ms,
        )
        result.provenance_hash = _compute_hash(result)

        logger.info(
            "Gap analysis complete: status=%s, gap=%.2f tCO2e (%.1f%%), "
            "budget_remaining=%.2f tCO2e",
            trajectory_status, float(current_gap), float(current_gap_pct),
            float(budget.budget_remaining_tco2e),
        )
        return result

    # ------------------------------------------------------------------ #
    # Year-by-Year Gap                                                    #
    # ------------------------------------------------------------------ #

    def _calculate_yearly_gaps(self, data: GapInput) -> List[YearlyGap]:
        """Calculate gap for each year from base year to target year.

        For past years, uses historical data if available; otherwise
        interpolates.  For future years, uses projection method.

        Args:
            data: Gap analysis input.

        Returns:
            List of YearlyGap entries.
        """
        gaps: List[YearlyGap] = []
        historical_map = {h.year: h.total_tco2e for h in data.historical_data}

        # Compute actual annual reduction trend for projection
        actual_annual_reduction = self._compute_actual_annual_reduction(data)

        for year in range(data.base_year, data.target_year + 1):
            expected = self._expected_at_year(
                data.base_year_total_tco2e,
                data.annual_rate_pct,
                data.base_year,
                year,
            )

            # Determine actual/projected emissions
            if year in historical_map:
                actual = historical_map[year]
            elif year == data.current_year:
                actual = data.current_total_tco2e
            elif year == data.base_year:
                actual = data.base_year_total_tco2e
            elif year > data.current_year:
                actual = self._project_emissions(
                    data, year, actual_annual_reduction
                )
            else:
                # Past year without data -- interpolate between base and current
                actual = self._interpolate_past(data, year)

            gap = _round_val(actual - expected)
            gap_pct = _round_val(_safe_pct(gap, expected), 2) if expected > Decimal("0") else Decimal("0")
            status = self._status_from_gap_pct(gap_pct)

            gaps.append(YearlyGap(
                year=year,
                expected_tco2e=_round_val(expected),
                actual_tco2e=_round_val(actual),
                gap_tco2e=gap,
                gap_pct=gap_pct,
                status=status,
            ))

        return gaps

    # ------------------------------------------------------------------ #
    # Scope-Level Gap                                                     #
    # ------------------------------------------------------------------ #

    def _calculate_scope_gaps(self, data: GapInput) -> List[ScopeGap]:
        """Calculate gap for each scope.

        Args:
            data: Gap analysis input with scope data.

        Returns:
            List of ScopeGap entries.
        """
        scope_gaps: List[ScopeGap] = []

        for scope in data.scope_data:
            expected = self._expected_at_year(
                scope.base_year_tco2e,
                scope.annual_rate_pct,
                data.base_year,
                data.current_year,
            )

            gap = _round_val(scope.current_tco2e - expected)
            gap_pct = _round_val(
                _safe_pct(gap, expected), 2
            ) if expected > Decimal("0") else Decimal("0")
            status = self._status_from_gap_pct(gap_pct)

            # Acceleration factor
            years_elapsed = data.current_year - data.base_year
            years_remaining = scope.target_year - data.current_year
            actual_reduction = scope.base_year_tco2e - scope.current_tco2e
            remaining_reduction = scope.current_tco2e - scope.target_tco2e

            acceleration = Decimal("1.0")
            if years_remaining > 0 and years_elapsed > 0:
                actual_rate = _safe_divide(
                    actual_reduction,
                    scope.base_year_tco2e * _decimal(years_elapsed) / Decimal("100"),
                )
                required_rate = _safe_divide(
                    remaining_reduction,
                    scope.current_tco2e * _decimal(years_remaining) / Decimal("100"),
                )
                if actual_rate > Decimal("0"):
                    acceleration = _round_val(
                        _safe_divide(required_rate, actual_rate), 2
                    )

            scope_gaps.append(ScopeGap(
                scope_label=scope.scope_label,
                base_year_tco2e=scope.base_year_tco2e,
                current_tco2e=scope.current_tco2e,
                expected_tco2e=_round_val(expected),
                target_tco2e=scope.target_tco2e,
                gap_tco2e=gap,
                gap_pct=gap_pct,
                status=status,
                required_acceleration=acceleration,
            ))

        return scope_gaps

    # ------------------------------------------------------------------ #
    # Budget Analysis                                                     #
    # ------------------------------------------------------------------ #

    def _calculate_budget(
        self, data: GapInput, budget_start: int
    ) -> BudgetAnalysis:
        """Calculate cumulative emissions budget to net zero.

        The pathway budget is the sum of expected annual emissions
        along the target pathway from budget_start to target_year.

        Args:
            data: Gap analysis input.
            budget_start: First year of the budget period.

        Returns:
            BudgetAnalysis with pathway and projected budgets.
        """
        years_remaining = max(0, data.target_year - data.current_year)
        actual_annual_reduction = self._compute_actual_annual_reduction(data)

        pathway_sum = Decimal("0")
        projected_sum = Decimal("0")

        for year in range(budget_start, data.target_year + 1):
            expected = self._expected_at_year(
                data.base_year_total_tco2e,
                data.annual_rate_pct,
                data.base_year,
                year,
            )
            pathway_sum += max(expected, Decimal("0"))

            if year <= data.current_year:
                projected_sum += data.current_total_tco2e
            else:
                proj = self._project_emissions(
                    data, year, actual_annual_reduction
                )
                projected_sum += max(proj, Decimal("0"))

        pathway_budget = _round_val(pathway_sum)
        projected_budget = _round_val(projected_sum)
        budget_gap = _round_val(projected_budget - pathway_budget)

        # Remaining allowance from current year forward
        remaining_sum = Decimal("0")
        for year in range(data.current_year, data.target_year + 1):
            expected = self._expected_at_year(
                data.base_year_total_tco2e,
                data.annual_rate_pct,
                data.base_year,
                year,
            )
            remaining_sum += max(expected, Decimal("0"))

        return BudgetAnalysis(
            budget_start_year=budget_start,
            budget_end_year=data.target_year,
            pathway_budget_tco2e=pathway_budget,
            projected_budget_tco2e=projected_budget,
            budget_gap_tco2e=budget_gap,
            budget_remaining_tco2e=_round_val(remaining_sum),
            years_remaining=years_remaining,
        )

    # ------------------------------------------------------------------ #
    # Risk Assessment                                                     #
    # ------------------------------------------------------------------ #

    def _assess_risk(
        self,
        data: GapInput,
        current_gap: Decimal,
        current_gap_pct: Decimal,
    ) -> RiskAssessment:
        """Produce an overall risk assessment.

        Args:
            data: Gap analysis input.
            current_gap: Current year gap in tCO2e.
            current_gap_pct: Current year gap as percentage.

        Returns:
            RiskAssessment with status, severity, and factors.
        """
        overall_status = self._assess_trajectory_status(current_gap_pct)
        severity = self._classify_severity(current_gap_pct)

        years_elapsed = max(1, data.current_year - data.base_year)
        years_remaining = max(0, data.target_year - data.current_year)

        # Actual annual reduction (absolute tCO2e/yr)
        total_reduction = data.base_year_total_tco2e - data.current_total_tco2e
        actual_annual = _round_val(
            _safe_divide(total_reduction, _decimal(years_elapsed))
        )

        # Required annual reduction from now
        remaining_reduction = data.current_total_tco2e - data.target_total_tco2e
        required_annual = Decimal("0")
        if years_remaining > 0:
            required_annual = _round_val(
                _safe_divide(remaining_reduction, _decimal(years_remaining))
            )

        # Acceleration factor
        acceleration = Decimal("1.0")
        if actual_annual > Decimal("0"):
            acceleration = _round_val(
                _safe_divide(required_annual, actual_annual), 2
            )

        # Time to target
        time_to_target = None
        if actual_annual > Decimal("0") and remaining_reduction > Decimal("0"):
            time_to_target = _round_val(
                _safe_divide(remaining_reduction, actual_annual), 1
            )

        # Confidence based on data availability
        confidence = "high"
        if len(data.historical_data) < 3:
            confidence = "medium"
        if len(data.historical_data) < 1:
            confidence = "low"

        # Risk factors
        risk_factors: List[str] = []
        if acceleration > Decimal("2.0"):
            risk_factors.append(
                f"Reduction must accelerate {acceleration}x to stay on track."
            )
        if current_gap > Decimal("0"):
            risk_factors.append(
                f"Currently {current_gap} tCO2e above target pathway."
            )
        if years_remaining <= 5 and severity in (
            GapSeverity.SIGNIFICANT.value, GapSeverity.CRITICAL.value
        ):
            risk_factors.append(
                "Less than 5 years remaining with significant gap."
            )
        if total_reduction <= Decimal("0"):
            risk_factors.append(
                "Emissions have not decreased since base year."
            )

        return RiskAssessment(
            overall_status=overall_status,
            severity=severity,
            time_to_target_years=time_to_target,
            required_annual_reduction_tco2e=required_annual,
            actual_annual_reduction_tco2e=actual_annual,
            acceleration_factor=acceleration,
            confidence=confidence,
            risk_factors=risk_factors,
        )

    # ------------------------------------------------------------------ #
    # Projection                                                          #
    # ------------------------------------------------------------------ #

    def _project_emissions(
        self,
        data: GapInput,
        year: int,
        actual_annual_reduction: Decimal,
    ) -> Decimal:
        """Project emissions for a future year.

        Args:
            data: Gap analysis input.
            year: Year to project.
            actual_annual_reduction: Observed annual reduction (tCO2e/yr).

        Returns:
            Projected emissions in tCO2e.
        """
        years_ahead = year - data.current_year

        if data.projection_method == ProjectionMethod.LINEAR:
            projected = data.current_total_tco2e - (
                actual_annual_reduction * _decimal(years_ahead)
            )
        elif data.projection_method == ProjectionMethod.FLAT:
            projected = data.current_total_tco2e
        elif data.projection_method == ProjectionMethod.COMPOUND:
            if data.current_total_tco2e > Decimal("0") and actual_annual_reduction > Decimal("0"):
                rate = _safe_divide(
                    actual_annual_reduction, data.current_total_tco2e
                )
                factor = (Decimal("1") - rate) ** years_ahead
                projected = data.current_total_tco2e * factor
            else:
                projected = data.current_total_tco2e
        else:
            projected = data.current_total_tco2e

        return max(projected, Decimal("0"))

