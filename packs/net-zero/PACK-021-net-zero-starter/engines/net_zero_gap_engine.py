    def get_summary(self, result: GapResult) -> Dict[str, Any]:
        """Generate a concise summary from a GapResult.

        Args:
            result: GapResult to summarize.

        Returns:
            Dict with key gap metrics and provenance.
        """
        summary = {
            "entity_name": result.entity_name,
            "current_year": result.current_year,
            "target_year": result.target_year,
            "trajectory_status": result.trajectory_status,
            "current_gap_tco2e": str(result.current_gap_tco2e),
            "current_gap_pct": str(result.current_gap_pct),
            "severity": result.risk_assessment.severity,
            "acceleration_factor": str(result.risk_assessment.acceleration_factor),
            "budget_remaining_tco2e": str(
                result.budget_analysis.budget_remaining_tco2e
            ),
            "years_remaining": result.budget_analysis.years_remaining,
            "scope_count": len(result.gap_by_scope),
        }
        summary["provenance_hash"] = _compute_hash(summary)
        return summary
