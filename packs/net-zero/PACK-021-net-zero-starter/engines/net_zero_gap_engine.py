    # Internal Helpers                                                    #
    # ------------------------------------------------------------------ #

    def _expected_at_year(
        self,
        base_tco2e: Decimal,
        annual_rate_pct: Decimal,
        base_year: int,
        year: int,
    ) -> Decimal:
        """Calculate expected emissions at a given year on the linear pathway.

        Formula: expected = base * (1 - annual_rate/100 * (year - base_year))

        Args:
            base_tco2e: Base year emissions.
            annual_rate_pct: Annual reduction rate (%).
            base_year: Base year.
            year: Year to calculate for.

        Returns:
            Expected emissions (floored at 0).
        """
        elapsed = _decimal(year - base_year)
        reduction_fraction = annual_rate_pct / Decimal("100") * elapsed
        expected = base_tco2e * (Decimal("1") - reduction_fraction)
        return max(expected, Decimal("0"))

    def _compute_actual_annual_reduction(self, data: GapInput) -> Decimal:
        """Compute actual annual reduction from base year to current year.

        Args:
            data: Gap analysis input.

        Returns:
            Annual reduction in tCO2e (may be negative if emissions rose).
        """
        years = max(1, data.current_year - data.base_year)
        total_reduction = data.base_year_total_tco2e - data.current_total_tco2e
        return _safe_divide(total_reduction, _decimal(years))

    def _interpolate_past(self, data: GapInput, year: int) -> Decimal:
        """Linearly interpolate emissions for a past year without data.

        Args:
            data: Gap analysis input.
            year: Year to interpolate.

        Returns:
            Interpolated emissions.
        """
        if data.current_year == data.base_year:
            return data.base_year_total_tco2e

        total_span = _decimal(data.current_year - data.base_year)
        elapsed = _decimal(year - data.base_year)
        fraction = _safe_divide(elapsed, total_span)

        return (
            data.base_year_total_tco2e
            + (data.current_total_tco2e - data.base_year_total_tco2e) * fraction
        )

    def _assess_trajectory_status(self, gap_pct: Decimal) -> str:
        """Map gap percentage to RAG status.

        Args:
            gap_pct: Gap as percentage of expected (positive = above).

        Returns:
            TrajectoryStatus value string.
        """
        if gap_pct <= Decimal("0"):
            return TrajectoryStatus.ON_TRACK.value
        elif gap_pct <= Decimal("20"):
            return TrajectoryStatus.AT_RISK.value
        else:
            return TrajectoryStatus.OFF_TRACK.value

    def _classify_severity(self, gap_pct: Decimal) -> str:
        """Classify gap severity from percentage.

        Args:
            gap_pct: Gap percentage.

        Returns:
            GapSeverity value string.
        """
        if gap_pct <= Decimal("0"):
            return GapSeverity.NONE.value
        elif gap_pct <= Decimal("10"):
            return GapSeverity.MINOR.value
        elif gap_pct <= Decimal("25"):
            return GapSeverity.MODERATE.value
        elif gap_pct <= Decimal("50"):
            return GapSeverity.SIGNIFICANT.value
        else:
            return GapSeverity.CRITICAL.value

    def _status_from_gap_pct(self, gap_pct: Decimal) -> str:
        """Determine year-level status from gap percentage.

        Args:
            gap_pct: Gap as percentage.

        Returns:
            Status string.
        """
        return self._assess_trajectory_status(gap_pct)

