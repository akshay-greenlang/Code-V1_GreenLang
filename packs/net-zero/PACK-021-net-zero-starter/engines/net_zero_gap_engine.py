class TrajectoryStatus(str, Enum):
    """RAG status for trajectory assessment.

    ON_TRACK (green): Current emissions at or below pathway.
    AT_RISK (amber): Current emissions 0-20% above pathway.
    OFF_TRACK (red): Current emissions >20% above pathway.
    ACHIEVED: Target already met.
    EXPIRED: Target year has passed without achievement.
    """
    ON_TRACK = "on_track"
    AT_RISK = "at_risk"
    OFF_TRACK = "off_track"
    ACHIEVED = "achieved"
    EXPIRED = "expired"

class GapSeverity(str, Enum):
    """Severity classification for the emissions gap.

    NONE: No gap (at or ahead of target).
    MINOR: Gap < 10% above pathway.
    MODERATE: Gap 10-25% above pathway.
    SIGNIFICANT: Gap 25-50% above pathway.
    CRITICAL: Gap > 50% above pathway.
    """
    NONE = "none"
    MINOR = "minor"
    MODERATE = "moderate"
    SIGNIFICANT = "significant"
    CRITICAL = "critical"

class ProjectionMethod(str, Enum):
    """Method used for future emissions projection.

    LINEAR: Simple linear extrapolation from recent trend.
    FLAT: Assumes current year emissions remain constant.
    COMPOUND: Compound annual reduction from current level.
    """
    LINEAR = "linear"
    FLAT = "flat"
    COMPOUND = "compound"

