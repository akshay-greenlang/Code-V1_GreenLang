"""
NetZeroGapEngine - PACK-021 Net Zero Starter Engine 3
=======================================================

Gap-to-net-zero analysis showing current emissions vs. required
target trajectory year-by-year with RAG status and budget tracking.

This engine compares actual emissions against the target pathway
generated by Engine 2 (NetZeroTargetEngine).  It calculates the
absolute and percentage gap, determines whether the organization
is on-track / at-risk / off-track (RAG status), projects future
emissions using linear extrapolation, and computes the remaining
cumulative emissions budget to net zero.

Calculation Methodology:
    Gap (absolute):
        gap_tco2e = current_tco2e - expected_tco2e_at_year

    Gap (percentage):
        gap_pct = gap_tco2e / expected_tco2e * 100

    Expected emissions at year Y (linear pathway):
        expected = base * (1 - annual_rate * (Y - base_year) / 100)

    Trajectory projection (linear extrapolation):
        projected[Y] = current - (annual_actual_reduction * (Y - current_year))

    Required acceleration:
        If behind: acceleration_factor = required_rate / actual_rate

    Budget remaining (cumulative):
        budget = sum of [linear_pathway(Y)] for Y in [current_year..target_year]

    Time to target:
        If actual annual reduction > 0:
            years = remaining_reduction / actual_annual_reduction

Regulatory References:
    - SBTi Corporate Net-Zero Standard v1.2 (2023) - Monitoring guidance
    - GHG Protocol Corporate Standard - Chapter 8 (Target Tracking)
    - CDP Climate Change 2024 Questionnaire - C4 Targets & Performance
    - TCFD Recommendations (2017) - Metrics and Targets

Zero-Hallucination:
    - All calculations use deterministic Decimal arithmetic
    - Linear interpolation only (no ML/LLM predictions)
    - SHA-256 provenance hash on every result
    - No LLM involvement in any calculation path

Author:  GreenLang Platform Team
Date:    March 2026
Pack:    PACK-021 Net Zero Starter
Status:  Production Ready
"""

