def _alpha_source_param_ids() -> List[Dict[str, Any]]:
    """Sorted list of alpha source raw dicts (stable test ordering)."""
    sources = alpha_v0_1_sources() or {}
    return [sources[k] for k in sorted(sources.keys())]


