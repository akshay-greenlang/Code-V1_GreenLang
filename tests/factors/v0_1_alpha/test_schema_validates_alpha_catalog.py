@pytest.mark.skip_unless_alpha_records_seeded
@pytest.mark.parametrize(
    "source",
    _alpha_source_param_ids(),
    ids=lambda s: str(s.get("source_id") or "<unknown>"),
)
def test_alpha_source_records_pass_provenance_gate(
    source: Dict[str, Any], gate: AlphaProvenanceGate
) -> None:
    """Every record from an alpha-source parser must pass the gate.

    Wave D / TaskCreate #6 — when the v0.1 catalog seeds under
    ``greenlang/factors/data/catalog_seed_v0_1/<source_id>/v1.json``
    exist, this test loads them directly (post-lift, contract-shaped)
    and the gate must pass STRICT (no xfail). Until the backfill seeds
    are generated, the test transparently falls back to running the
    raw v1 parser and the gate will fail; in that state the test signals
    the regression by failing — not via xfail.
    """
    source_id = source.get("source_id") or "<unknown>"
    records, status = _load_records_for_source(source)

    if status == "no_seed":
        pytest.skip(
            f"no alpha seed input for {source_id} at "
            f"{_seed_path_for(source_id).relative_to(_REPO_ROOT)}"
        )
    if status == "parser_error":
        pytest.skip(
            f"parser for {source_id} could not be exercised in unit-test "
            f"scope (see captured logs for the underlying cause)"
        )

    assert records, f"no records produced by {source_id}"

    failures: List[Tuple[int, List[str]]] = []
    for i, rec in enumerate(records):
        errs = gate.validate(rec)
        if errs:
            failures.append((i, errs[:3]))  # cap each record's errors

    assert not failures, _format_failure_report(source, len(records), failures)


# ---------------------------------------------------------------------------
# Aggregate sanity check — produces the per-source pass/fail summary in the
# test logs even when the parametrised cases xfail. This test is NOT
# xfailed because it just verifies the test harness itself can execute
# the parsers and produce a report; the assertion only fires if EVERY
# alpha source has zero records (a config regression).
# ---------------------------------------------------------------------------


def test_alpha_catalog_summary_can_be_produced(
    gate: AlphaProvenanceGate, caplog: pytest.LogCaptureFixture
) -> None:
    """Smoke-test the harness: every alpha source must yield SOMETHING.

    Either records (which then go through the gate) or a clean
    ``no_seed`` / ``parser_error`` status. The test fails only when the
    registry-declared 6 alpha sources collapse to zero — a config
    regression that would silently disable the CI gate.
    """
    sources = _alpha_source_param_ids()
    assert len(sources) == 6, (
        f"expected 6 alpha sources from alpha_v0_1_sources(); got {len(sources)}"
    )

    rows: List[Dict[str, Any]] = []
    overall_failure_modes: Counter = Counter()
    for source in sources:
        source_id = str(source.get("source_id") or "<unknown>")
        records, status = _load_records_for_source(source)
        passed = 0
        failed = 0
        if status == "ok":
            for rec in records:
                errs = gate.validate(rec)
                if errs:
                    failed += 1
                    overall_failure_modes.update(_failure_keys(errs))
                else:
                    passed += 1
        rows.append(
            {
                "source_id": source_id,
                "status": status,
                "total": len(records),
                "passed": passed,
                "failed": failed,
            }
        )

    # Log the report at WARNING so it surfaces in CI without an extra flag.
    logger.warning("ALPHA-CATALOG GATE SUMMARY:")
    logger.warning(
        "%-28s | %-12s | %5s | %5s | %5s",
        "source_id", "status", "total", "pass", "fail",
    )
    logger.warning("-" * 72)
    for r in rows:
        logger.warning(
            "%-28s | %-12s | %5d | %5d | %5d",
            r["source_id"], r["status"], r["total"], r["passed"], r["failed"],
        )
    if overall_failure_modes:
        logger.warning("TOP FAILURE MODES:")
        for mode, count in overall_failure_modes.most_common(5):
            logger.warning("  %4d x %s", count, mode)

    # Require AT LEAST ONE source to have produced records. Zero across
    # the board means the test harness is broken (parser dir gone,
    # seeds purged, etc.) — that IS a regression worth blocking on.
    assert any(r["total"] > 0 for r in rows), (
        "ALL alpha sources produced 0 records — alpha CI gate is no-op. "
        f"per-source rows: {rows}"
    )


_REQUIRED_PROP_RE = re.compile(r"'([^']+)' is a required property")
_ADDITIONAL_PROPS_RE = re.compile(r"^Additional properties are not allowed")


def _failure_keys(errs: List[str]) -> List[str]:
    """Reduce each failure message to a stable, aggregable key.

    The gate produces messages like::

        schema[<root>]: 'urn' is a required property
        schema[<root>]: Additional properties are not allowed ('factor_id', ...)
        schema[extraction.parser_commit]: must be 7-40 hex...
        alpha[review.approved_by]: required when review_status == 'approved'
        alpha[gwp_basis]: must be 'ar6' in v0.1 alpha; got 'ar5'

    For aggregation, we want one bucket per *distinct missing field /
    distinct constraint violation*. Strategy:

    * If the message contains ``'<X>' is a required property``, bucket as
      ``missing[<X>]`` (independent of which path it surfaced under).
    * If the message starts with ``Additional properties...``, bucket as
      ``schema[additionalProperties]`` (the v1->v0_1 shape mismatch).
    * Otherwise, fall back to the leading ``<layer>[<path>]`` prefix.
    """
    out: List[str] = []
    for e in errs:
        head, sep, tail = e.partition(":")
        body = (tail or "").strip()
        m = _REQUIRED_PROP_RE.search(body)
        if m:
            out.append(f"missing[{m.group(1)}]")
            continue
        if _ADDITIONAL_PROPS_RE.match(body):
            out.append("schema[additionalProperties]")
            continue
        out.append(head.strip() if sep else e.strip())
    return out
